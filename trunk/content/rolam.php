<p>
Marosvölgyi Gergely vagyok, 1992. augusztus 12-én születtem Vácott.
A Dunakanyar szívében, Nagymaroson nőttem fel, jelenleg is ott élek.
</p>

<p>
Iskolás éveimet a nagymarosi Kittenberger Kálmán Általános és Művészeti Iskolában kezdtem 1999-ben,
ahol már alsósként is többször szerepeltem valamilyen formában: színdarabban, ünnepségeken, versmondó versenyeken.
Hittem benne, hogy nekem ezzel még dolgom lesz, de akkoriban igencsak a „futottak még” kategóriába tartoztam.
<a href="vers/a-szomszed">Életem első versét</a> is ebben az időszakban írtam &ndash; 2002-ben &ndash;,
de néhány további gyenge próbálkozás után 2003 környékén rájöttem, hogy ehhez még túl tapasztalatlan vagyok,
ezért pár évig félretettem az írást. Gondoltam: megvárom, míg eljön az én időm...
</p>

<p>
Tanulmányaimat 2007-től a váci Boronkay György Műszaki Középiskolában folytattam,
angol nyelvi előkészítőben, majd átmentem programozó osztályba. Előbb középfokú, majd
végzősként &ndash; tanárom biztatására &ndash; felsőfokú nyelvvizsgát is szereztem angolból.
Eleinte nagyon nem ment a programozás, aztán valahogy mégis ráéreztem, és végül olyannyira megszerettem,
hogy már akkor elhatároztam: ezzel (is) szeretnék foglalkozni.
</p>

<p>
2010 tavaszán középiskolánkban kiírtak egy vers- és novellaíró pályázatot, és úgy gondoltam, itt az idő...
Kiírtam magamból mindent, ami akkor a lelkemet nyomta, és így született meg <a href="vers/vandorlelek">Vándorlélek</a> c. versem,
amiért különdíjat kaptam. Ma már nem sorolnám legjobb alkotásaim közé, mindenesetre akkor nekem mérföldkőnek számított, mert onnantól kezdve rendszeresen írtam.
</p>

<p>
Ugyancsak 2010 körülre tehető, amikor elhatároztam, hogy jobb versmondó szeretnék lenni.
Ehhez elkezdtem a „nagyokat” hallgatni, főleg Latinovits Zoltán, Sinkovits Imre, Szakácsi Sándor és Kaszás Attila versmondásait,
és próbáltam ellesni a fogásokat, amiket aztán elkezdtem beépíteni saját produkcióimba.
A változás döbbenetes volt számomra: 11. osztályban már 2., végzősként pedig 1. helyezett lettem az iskolai versmondó versenyen.
Ez óriási lökést adott, és megerősített utamban...
</p>

<p>
2012-ben jelentkeztem Veszprémbe, a Pannon Egyetem Műszaki Informatikai Karára,
ahol aztán 2016-ban szereztem meg programtervező informatikus szakos diplomámat.
Azt hiszem, az egyetemi években kezdtem el igazán kibontakozni, mind írás, mind pedig versmondás terén.
2012 novemberében vettem részt életem első &ndash; Pintér Tibor nevével fémjelzett &ndash; országos versmondó versenyén,
ahol Szilvásiné Csordás Éva (akkor még ismeretlenül) felvetette, hogy lehetnék az irodalmi színpad tagja.
Fél évvel később így is lett, és 2013 februárjában csatlakoztam az 1958 óta fennálló Váci Mihály Irodalmi Színpadhoz.
Náluk tanultam meg a versmondás fortélyait, és általuk erősödött meg a versek iránti szeretetem.
</p>

<p>
Onnantól kezdve minden évben jártam valamilyen országos versmondó versenyen, hogy megmérettessem magam,
így aztán 2016. november 11-én megkaptam az ország legjobb versmondójának járó Radnóti-díjat, 2018. június 10-én pedig a Latinovits-díjat.
</p>

<p>
A versek pedig apránként gyűltek, és így már szerencsére nemcsak versmondással,
de saját írásaimmal is közönség elé léphettem.
2012 tavaszán egy országos „Szülőföldem” c. pályázaton 2. helyezést értem el <a href="vers/nagymaros">Nagymaros</a> c. versemmel,
majd 2015 elején megkaptam az Irodalmi Rádió „Az év diák költője 2014” díját, <a href="vers/hivo-szo">Hívó szó</a> c. versemért.
</p>

<p>
Mindvégig egyvalami lebegett a szemem előtt: hittem benne, hogy egyszer sikerül maradandót alkotnom,
mert &ndash; még a sok bizonytalanságom ellenére is &ndash; úgy érzem, hogy én ezt a „hivatást” kaptam a sorstól,
és míg az a hang belülről súg valamit, addig az papírért kiált...
</p>

