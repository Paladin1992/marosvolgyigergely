<p>
    Marosvölgyi Gergely vagyok, informatikus, aki szabadidejében szeret verseket, novellákat írni.
</p>

<p>
    Ezt a weboldalt azért hoztam létre, hogy legyen egy hely,
    ahová saját ízlésem szerint tehetem fel írásaimat,
    hogy azok könnyedén elérhetők legyenek bárki számára,
    regisztráció vagy más egyéb kötöttségek nélkül.
</p>

<p>
    <a href="versek">Verseimet</a> és <a href="novellak">novelláimat</a> értelemszerűen a megfelelő menüpont alatt találod.
</p>

<p>
    Ha többet szeretnél megtudni rólam, akkor olvasd el az <a href="rolam">önéletrajzomat</a>.
</p>

<blockquote>
    <p class="intro-poem clearfix">
        Én költő vagyok, de nem kell dicsőség,<br>
        ne ünnepelje bennem senki hősét,<br>
        ily ünneplést én Istenre hagyok.<br>
        Ő az, hogy nincs nagy, kinél nincs nagyobb.<br>
        Versem azé, ki szivem versbe kérte<br>
        és nékem elég a barátság érte.<br>

        <span class="intro-poet">&mdash; József Attila</span>
    </p>
</blockquote>