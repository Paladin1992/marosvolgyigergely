<h3>Ars poetica</h3>

<p>
Mint földre hulló dicső jel:<br>
elvarázsolt égi múzsa,<br>
megáldott egy ős-erővel,<br>
s ösztönömmel kötött gúzsba.
</p>

<p>
Sajgó szívem lángját szórván,<br>
mint forrásból zúg friss patak<br>
&ndash; túl sok éjen s még több órán &ndash;,<br>
lapra folytak néma szavak.
</p>

<p>
Tengerré nőtt mind a vágy, mely<br>
bennem lapult kimondatlan;<br>
sors keze ha messze ránt el,<br>
zeng belőlem tiszta dallam.
</p>

<p>
Szavam küldöm biztatónak<br>
&ndash; hátha van, mely ideális:<br>
adjon enyhet olvasómnak<br>
dűnék között hűs oázis...!
</p>

<p>
Mert tudd: a szó is hatalom,<br>
nem csak önmagának éke;<br>
e gyertyalángnyi oltalom<br>
magyarságunk menedéke!
</p>

<p>
Felejts hát el minden harcot &ndash;<br>
hisz elég harc, hogy élni kell;<br>
lelkem vizén mosd meg arcod,<br>
s meglásd: szíved újra kikel!
</p>

<p>
Megküzdök a mindenséggel<br>
&ndash; „i” leszek a gazság előtt &ndash;,<br>
s győzni fogok reménységgel,<br>
mint örök Nap, mely éjbe nőtt!
</p>

<p>
Lelkem a szépség zászlaja &ndash;<br>
hiába tépi kor szele:<br>
se csürhe bódító zaja,<br>
sem idő nem kezd ki vele!
</p>

<p>
Kultúránk ma sivár birtok:<br>
szép virágnak alig kedvez;<br>
amíg bírom, gyomot irtok &ndash; <br>
de ahhoz már te is kellesz!
</p>

<p>
Fogd a kezem, segíts nekem!<br>
Suhanjunk a tettek szárnyán &ndash;<br>
a szép, magyar szó kincs legyen:<br>
méltón ékes fény-szivárvány!
</p>

<p>
Sorsom lesz az áldozatom,<br>
s vele együtt hitem, bűnöm;<br>
beragyogom csillagzatom,<br>
s megpihenek lágy betűkön...
</p>

<p>
Még nem tudom, hogy feledés<br>
vagy mély öröklét itat át,<br>
de míg lehet: verselek &ndash; és<br>
megőrzöm a harmóniát.
</p>

