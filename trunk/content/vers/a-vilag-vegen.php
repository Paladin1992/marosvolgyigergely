<h3>A világ végén</h3>

<p>
A világ végén van egy hely,<br>
hol minden utunk véget ér &ndash;<br>
keresheted, mégsem leled:<br>
egy hely, mi benned s bennem él.
</p>

<p>
A világ végén van egy hely,<br>
hol otthon van a hontalan;<br>
hol legfőbb munka szeretni,<br>
és mind, ki gondos: gondtalan.
</p>

<p>
A világ végén van egy hely,<br>
hol nincs titok, mi rejtőzik &ndash;<br>
minden kérdés válaszra lel,<br>
s a becsületet megőrzik.
</p>

<p>
A világ végén van egy hely,<br>
hol az álom színekké lett,<br>
vágy tölti a kutak vizét,<br>
s rügyezik a boldog élet.
</p>

<p>
A világ végén van egy hely,<br>
hol együtt zsongnak évszakok,<br>
csillámpor hull minden ágról,<br>
és éjbe folynak nappalok.
</p>

<p>
A világ végén van egy hely,<br>
hol nem múlnak az emlékek,<br>
hol újra él, ki elment rég:<br>
csak mese száll és lágy ének.
</p>

<p>
A világ végén van egy hely,<br>
hol minden ember békét lel;<br>
hol nincs búcsú, csak lágy mosoly,<br>
és minden szívet vágy érlel.
</p>

<p>
A világ végén van egy hely,<br>
hol szállunk, lelket kitárván:<br>
erős várként áll az ima,<br>
és hitből nő a szivárvány.
</p>

<p>
A világ végén van egy hely,<br>
hol minden félből egész lesz:<br>
hol áldás hull és szeretet,<br>
és minden csodát megélhetsz...
</p>

<p>
S ha tényleg él e csodás hely,<br>
távol, a világ legvégén,<br>
majd tárt karokkal várok rád,<br>
míg együtt leszünk &ndash; te meg én.
</p>

