<h1>Fájjon, ha kell</h1>

<p>
Tőlem ne várjon jó szót senki:<br>
„Alkotni vagyunk, nem dicsérni”!
</p>

<p>
Telik az idő: nap, a hét is,<br>
s két halál közt halál a lét is!
</p>

<p>
Biz’ sok a dolgunk: vár a karma &ndash;<br>
mert csak a jövőnk teli kamra!
</p>

<p>
Az ajtódon tán remény kopog?<br>
(Csak esőt hallasz &ndash; Isten zokog!)
</p>

<p>
Kérni kérünk, de ritkán adunk,<br>
a szívben harag, s harcol agyunk.
</p>

<p>
Lásd, már eget-földet vér takar:<br>
mi öljük egymást, Ő jót akar.
</p>

<p>
...Legyek erős? Már nagyon unom,<br>
de csak ez az út: tudom, tudom...
</p>

<p>
Majd fájjon, ha kell &ndash; hát nem bánom:<br>
ha szebb jövőnk lesz, én megvárom!
</p>

