<p>
<span class="verse-number">1</span><br>
Lám, a herceg lép be feldúltan:<br>
„Hallottam én! Őt szereted?!”<br>
A lány orcáján keze csattan,<br>
Bús szeméből könny indul meg.<br>
Anyegin, látván ezt, felbőszült,<br>
A hercegnek ronta, mint őrült!<br>
S az gyáván rohan fiókhoz,<br>
Teli pisztolyt ránt marokhoz.<br>
„Nyomurult életednek vége!”<br>
Húzza ujját a ravaszon,<br>
&ndash; Vert mindnyájuk szíve nagyon &ndash;<br>
A pisztolya elsült hát végre...<br>
A lány golyó elé ugrott,<br>
S hercegétől lövést kapott...<br>
</p>

<p>
<span class="verse-number">2</span><br>
Tanja földre rogyott sápadtan,<br>
A herceg csak rettegve állt:<br>
Mit tett? Biz’ a’ felfoghatatlan!<br>
Felesége nem félt halált.<br>
Anyegint védte, mert szerette,<br>
S most félholtan fekszik előtte...<br>
A hős a férjnek nekiront,<br>
És lendületből üt nagyot;<br>
Pisztolya annak földre esett:<br>
Az erkélyajtón törtek át,<br>
S a herceg megüté magát;<br>
A teraszon feküdt, remegett,<br>
(Ok volt rá) Anyegintól félt.<br>
Mit lát benne? Elszánt hóhért!<br>
</p>

<p>
<span class="verse-number">3</span><br>
Jevgenyij hát a fegyverért nyúl,<br>
A hercegnek pisztolyáért,<br>
S nem szán neki reményt ezentúl &ndash;<br>
„Ezt kapod most Tatjanáért!”<br>
A férj közben már lassan felállt,<br>
S csak halkan mormolt el egy imát,<br>
De Anyegin tűzfűtve lőtt:<br>
(Nincs most senki a ház előtt<br>
Hisz’ fagyos idő van odaki...)<br>
A golyó szívébe nyesett,<br>
S a kőkorláton átesett.<br>
Éltét Anyegin így olta ki...<br>
Bosszúja hát beteljesült,<br>
Ám kedvese megsebesült.<br>
</p>

<p>
<span class="verse-number">4</span><br>
Terem ott gyorsan Tatjanánál,<br>
Nézi, hol érte találat:<br>
Lát vérveres foltot vállánál,<br>
Az Égieknek hálát ad!<br>
Mert ha szerelme előbb hal meg,<br>
Mintsem őt ölné a bánat meg...<br>
Élni? Akarna már soha!<br>
Ám tudná, mért ily mostoha<br>
Szerencsétlen földi élete!<br>
De hál’ Égnek: a lány jól van...<br>
Lelkét adta neki csókban,<br>
S miljom könnyet hullajta érte!<br>
Ki mindig szerette (még él)<br>
Mellette térdelve remél.<br>
</p>

<p>
<span class="verse-number">5</span><br>
Lelke a dühnek tüzében ég,<br>
Hogy e golyót nem ő kapta,<br>
&ndash; E gondolat emészti őt rég &ndash;<br>
Ez, mi szívét kínként marja...<br>
De időben ugrunk előre,<br>
Csak egy hónappal a jövőbe:<br>
Esküvő ez &ndash; hőseinké,<br>
Anyeginé s Tatjanáé.<br>
Mosttól már egymáséi lettek,<br>
S templomharang értük kondult...<br>
A golyólövés bár gyógyult,<br>
Gyötört szívük sebe nem hegedt<br>
Attól, min eddig átmentek,<br>
Csak kínt hozott és sérelmet.<br>
</p>

<p>
<span class="verse-number">6</span><br>
De telt hát megint, múlt az idő,<br>
&ndash; A párról is mindjárt szólok &ndash;<br>
Beköszöntött egy új esztendő,<br>
Figyelj arra, mit most mondok:<br>
Távol vannak már orosz hontól,<br>
Örökre távol minden rossztól.<br>
Itáliába mentek el,<br>
Világra jött gyermekükkel,<br>
Ki napvilágot ott látta meg<br>
Rómának tüzes fényében,<br>
S nem Pétervár zord telében.<br>
Végre a Sors őket álda meg,<br>
Útjaik hogyha követted,<br>
Történetük átélhetted.<br>
</p>

<p>
<span class="verse-number">7</span><br>
Kedves olvasóm, végezetként<br>
Bölcs szavaim jegyezd hát fel:<br>
&ndash; Kérlek, tartsd ezt meg tanulságként,<br>
S majd éltedben se felejtsd el &ndash;<br>
„Mindig csak hallgass a szívedre,<br>
Mert sohsem tudsz tenni ellene;<br>
Bár látnok ugyan nem lehetsz:<br>
De lesz jó út, mit meglelhetsz,<br>
Sorsod tied, ne menj az árral,<br>
Lelked tűzben ne hagyd égni:<br>
Hű legyél csak, s nincs ok félni!<br>
Becsüld, ha rálelsz boldogságra!”<br>
Ez lelt rá az ifjú párra,<br>
Anyeginra s Tatjánára...<br>
</p>

