<h1>Marosvölgyi Gergely</h1>

<p>
Vidám s jó fiú volt,<br>
bár néha kicsit nyers,<br>
lelkéhez szólt mélyen<br>
pár örök-igaz vers.
</p>

<p>
Ábrándozva nézett<br>
minden naplementét,<br>
s csak az írott szóban<br>
látta álma rendjét.
</p>

<p>
Lágyan szólt a hölgyekhez<br>
(romantika nemzette),<br>
mit szíve tüze diktált:<br>
tétován, de megtette.
</p>

<p>
Csekély életén kívül<br>
nem akadt itt más dolga &ndash;<br>
szeretni szerelemmel:<br>
csak ezt szeretett volna...
</p>

<p class="writing-info">
Veszprém,<br>
2013. június 23-24.
</p>