<p>
Kicsit ritka fajta<br>
vagyok, s mint a balta:<br>
fekszem nehéz fejjel<br>
(fel senki nem emel),<br>
nyelem a fájdalom<br>
hasító kínjait,<br>
míg várok valakit,<br>
kinek tetszik dalom<br>
minden apró sora &ndash;<br>
vagy csak annak pora.<br>
</p>

<p>
Dicső múltra várva,<br>
szép jövőben járva<br>
a jelent feledem<br>
(csak értetlenkedem),<br>
és Istenben bízva<br>
&ndash; mint örök idegen &ndash;<br>
<i>hisz</i> lelkem idelenn<br>
a békében, s míg a<br>
semmi szélén ülök,<br>
magammal békülök.<br>
</p>

<p>
A tömegben állok,<br>
s hazugokat látok,<br>
bár hiszik, hogy nagyok<br>
(sötét fényük ragyog),<br>
mert sokakat vakít<br>
vér és gyilkos pénzek<br>
&ndash; ezért ölni készek &ndash;;<br>
a vágy, mi szétszakít:<br>
s fekete lángokban<br>
élünk mi &ndash; romokban.
</p>

