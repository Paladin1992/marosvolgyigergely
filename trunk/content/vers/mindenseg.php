<p>
<span class="verse-subtitle">(Vers)</span><br>
Lehet grafit, lehet tinta:<br>
gondolatom mindig tiszta;<br>
lapra vésve feketéllek:<br>
felolvasnak, s újra élek.<br>
</p>

<p>
<span class="verse-subtitle">(Hangjegy)</span><br>
Vonásként, mi néma vagyon,<br>
kapaszkodom öt vonalon:<br>
társaimmal egybenőve<br>
dallá leszünk egy-kettőre.<br>
</p>

<p>
<span class="verse-subtitle">(Tűz)</span><br>
Halkan izzom: parázs vagyok,<br>
fát rágcsálva feltámadok,<br>
pici lángból nagy tűz leszek:<br>
égig nyúlva énekelek.<br>
</p>

<p>
<span class="verse-subtitle">(Víz)</span><br>
Folydogáló patak vagyok,<br>
tóba érve elszunnyadok,<br>
egyszer talán eső leszek:<br>
társaimmal lecsöppenek.<br>
</p>

<p>
<span class="verse-subtitle">(Virág)</span><br>
Magocskából virág leszek,<br>
simogatnak finom szelek,<br>
erdőn-mezőn nyílok másnak:<br>
születésnek, elmúlásnak.<br>
</p>

<p>
<span class="verse-subtitle">(Falevél)</span><br>
Lehulltam én ősi fámról,<br>
szél repít el szép hazámtól;<br>
ezer színem fényben ragyog:<br>
zizzenő kis levél vagyok.<br>
</p>

<p>
<span class="verse-subtitle">(Madár)</span><br>
Repülés az én hatalmam,<br>
kék égbolt a birodalmam:<br>
rab madár, ki mindig szabad –<br>
lelkem szárnyal, szívem marad.<br>
</p>

<p>
<span class="verse-subtitle">(Csillag)</span><br>
Csak szikrázom a semmiben:<br>
sugárzik erőm, nem pihen:<br>
messzire elér fény-kezem,<br>
s lehet, már nem is létezem...<br>
</p>

<p>
<span class="verse-subtitle">(Gyermek)</span><br>
Gyermek vagyok: kicsi, édes,<br>
szeretetre mindig éhes...<br>
Remény tüze lobog bennem –<br>
játsszuk el, hogy szeretsz engem!<br>
</p>

<p>
<span class="verse-subtitle">(Szülők)</span><br>
A tükörben őket látom,<br>
ölelésük mindig vágyom;<br>
az idő bár velünk sétál,<br>
öröklétük bennem él már!<br>
</p>

<p>
<span class="verse-subtitle">(Isten)</span><br>
Semmi vagyok, mégis minden:<br>
bennem él az igaz Isten;<br>
végtelen a szeretetem –<br>
ami nincs, majd megteremtem!<br>
</p>

<p>
<span class="verse-subtitle">(Egész)</span><br>
Szívemet, ha megöleled,<br>
én leszek a másik feled –<br>
s ha lelkünk örök tengerész,<br>
a lét vizével lesz egész!<br>
</p>

