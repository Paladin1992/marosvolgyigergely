<h3>Lesz helyed<br>
<span class="subtitle">(F. R. részére)</span></h3>

<p>
Már nem is tudom, mióta<br>
próbállak nem szeretni téged,<br>
de mindhiába küzdök én:<br>
s a kín csak egyre jobban éget!
</p>

<p>
Tudom, láthattam volna, hogy<br>
elmúlt szemedben a forró nyár,<br>
s vele hervadt minden rózsa<br>
benned, mik nem nyílnak újra már...
</p>

<p>
A nap se kel oly boldogan,<br>
mikor hív a reggel zord szava:<br>
némán dalol minden madár,<br>
s elfedi lelkem a bú hava.
</p>

<p>
Mióta elmentél, csupán<br>
vacogni jár belém a lélek,<br>
hisz nem karolsz át melegen,<br>
s a hiányoddal élni félek.
</p>

<p>
...Érzem még lágy ajkad ízét,<br>
perzselőn, ahogy számra tapadt,<br>
s a csóknyi vágyak tengere<br>
kiapad a szájpadlat alatt.
</p>

<p>
Hittem, hogy mindig karomban<br>
tarthatlak, óvhatlak ölelve,<br>
s majd boldogságot lelhetünk,<br>
mit fel nem foghat földi elme...
</p>

<p>
A sok jó már mind tovaszállt,<br>
lelkem kismadárként megriadt,<br>
s fájdalom járja testemet<br>
egy elmaradt ölelés miatt.
</p>

<p>
De tudd, hogy most is örülök,<br>
mert szép lelkeddel voltam áldott,<br>
s úgy éreztem néha, mintha<br>
veled ölelném a világot!
</p>

<p>
Bár nem merültem bele még<br>
könnyeim zúgó patakjába,<br>
arra kérlek, legyél boldog! &ndash;<br>
Csupán ezt foglalom imába.
</p>

<p>
Angyalom voltál, s millió<br>
színeddel kifestetted lelkem,<br>
s e színek nem fakulnak ki,<br>
amíg lesz helyed... a szívemben!
</p>

