<h1>Szívvel-lélekkel</h1>

<p>
Szerettél már nőt igazán?<br>
Dobbant érte szíved hangosan? <br>
A mindennapok hajnalán<br>
ébredtél mellette boldogan?
</p>

<p>
Fogtad puhán finom kezét,<br>
vagy ölelted-e magányában,<br>
mikor elcsendül a beszéd<br>
a csillaggal telt félhomályban?
</p>

<p>
Néztél-e rá, mint angyalra,<br>
mely égő pokoltól is megvéd,<br>
s fakadtál-e néha dalra,<br>
midőn rád hintette szerelmét?
</p>

<p>
Lested apró lépéseit<br>
és vele minden mozdulatát?<br>
Felcsillant benned a zenit,<br>
míg csodálattal ittad szavát?
</p>

<p>
Láttál-e sírva büszke nőt,<br>
kinek lágy lelkébe tiportál?<br>
(Pedig csak ő adott erőt,<br>
mikor te gyönge férfi voltál!)
</p>

<p>
Mondod-e büszkén, hogy érte<br>
akár az életed is adnád?<br>
S mikor a sors keze kérte:<br>
tisztelted mint gyermeked anyját?
</p>

<p>
...Hiúságod vesd le végleg,<br>
és most már ez égi megmentőt,<br>
kit Isten teremtett néked:<br>
becsüld meg, vigyázd, és szeresd őt!
</p>

<p>
Hagyd kinyílni mosolyát ma!<br>
Ne bizonyíts szóval &ndash; csak tettel,<br>
mert csak akkor látsz csodát, ha<br>
szereted őt... szívvel-lélekkel.
</p>

