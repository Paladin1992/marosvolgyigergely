<h1>Elégetlek!<br>
<span class="subtitle">Sz. K. részére</span></h1>

<p>
csak most<br>
csak most az egyszer leírom<br>
mert tovább már nem bírom<br>
hogy mi voltál és mennyire fáj<br>
(már készül a vihar és reszket a táj)<br>
mert elegem van<br>
belőled<br>
de főleg<br>
az epedésből<br>
a reménykedésből<br>
mert ezer sebből ömlik a vér<br>
ahogy tekinteted szívemig ér<br>
mert ez már nem álom<br>
s kívánom<br>
hogy árasszon el a félelem<br>
s a kegyetlen átok<br>
amit rád kiáltok<br>
mert most nem látok<br>
jót se szépet<br>
mert utál engem az élet<br>
mint én utállak téged
</p>

<p>
vártalak<br>
te ostoba női alak<br>
s örömtelen múltak el az évek<br>
miattad kerültek a szép emlékek<br>
s csak engem vár a szakadék<br>
és bármit megadnék<br>
hogy kijussak belőle<br>
de már látom: te vagy ketrecem őre<br>
szárnyaimat te tépted le<br>
hogy úgy repülj a végtelenbe<br>
pedig képtelen vagy szeretni<br>
de megtanulok feledni<br>
és fájni fog<br>
ahogy fáj nekem<br>
e gyötrelem<br>
mert akarom hogy neked is fájjon<br>
miként élet fáj a halálos ágyon
</p>

<p>
körbevesznek<br>
engem lesnek<br>
démoni szemek<br>
s éles karmaikkal vésik belém<br>
a szavakat hogy füledbe súgjam<br>
amit mondanak a jelek:<br>
arra sem vagy méltó hogy gyűl-öljelek
</p>

<p>
de még nincs vége<br>
még nincs béke<br>
még csak most jön a vihar<br>
a vihar<br>
mely elsöprő és könyörtelen<br>
mint te voltál velem<br>
és megismered lelkem<br>
tajtékzó tengerét<br>
mikor szívem dörgeti sötét egét<br>
és akkor futhatsz majd<br>
és nem menekülsz<br>
mert előbb-utóbb elé kerülsz<br>
és az lesz a szép<br>
ahogy felemészt és széjjeltép<br>
és boldog leszek<br>
és felnevetek<br>
hogy a mindenség is beleremeg!!...
</p>

