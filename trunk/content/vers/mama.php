<h3>Mama<br>
<span class="subtitle">(Nagymamám, sz. Paulicsek Rozália emlékére,<br>
1928. május 17. &ndash; 2012. április 22.)</span></h3>

<p>
Most is a mamára gondolok<br>
sok más egyéb dolgom között,<br>
ki hagyván e földi kínokat<br>
egy szebb világba költözött.
</p>

<p>
Még mindig látom magam előtt,<br>
ahogy tesz-vesz kint a kertben:<br>
megáll néha, s izzadt homlokát<br>
törli fáradtságtól telten.
</p>

<p>
A meleg napfény rámosolyog,<br>
és derű ragyog vén arcán...<br>
(Még tavasz volt, hogy eltávozott,<br>
s már őszi szél ül kint a fán.)
</p>

<p>
Fakó képek tömlöcéből a<br>
múlt most mereven rám mered,<br>
s az emlékek örök futára<br>
könnyek árjaként megered...
</p>

<p>
Néma áhítattal szemlélem<br>
e csillaggal telt szép eget,<br>
s mosolyogva látom őt, amint<br>
új otthonából integet.
</p>

<p class="writing-info">
Nagymaros, Veszprém,<br>
2012. május 17-23., 26.<br>
2012. október 10., 25-26.<br>
2012. november 15-16.
</p>