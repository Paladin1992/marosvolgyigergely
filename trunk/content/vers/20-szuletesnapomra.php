<h3>20. születésnapomra</h3>

<p>
Kerek húsz éves lettem én,<br>
„nagy” ajándék e költemény,<br>
mellyel &ndash; kissé alaptalan &ndash;<br>
meglepem hát magam, magam.
</p>

<p>
Bár húsz esztendőm hatalom,<br>
jobb lesz, ha mégis eladom!...<br>
Kétlem, hogy kéne bárkinek:<br>
tán az ördög se veszi meg!
</p>

<p>
Potyásként az élet hátán,<br>
csak fekszem vágyaim ágyán.<br>
Már jó ideje nincs hitem,<br>
és nem bízom meg senkiben.
</p>

<p>
Időmet csak itthon töltöm,<br>
de sosem jártam külföldön;<br>
s bár hittem, talán meglelhet:<br>
még nem éltem meg szerelmet...
</p>

<p>
Ürességtől reménytelten<br>
lengedez a ködben lelkem.<br>
Az idő lassan elvakít,<br>
s epedve várok valakit.
</p>

<p>
Dacolván e zord világgal:<br>
erdőm tele száraz ággal.<br>
Saját láncaimnál fogva<br>
maradok a magam foglya.
</p>

<p class="writing-info">
Nagymaros,<br>
2012. augusztus 12.
</p>