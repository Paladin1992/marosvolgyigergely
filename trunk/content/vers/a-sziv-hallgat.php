<h1>A szív hallgat</h1>

<p>
Már csak Te vagy, Uram,<br>
ki segíthetsz rajtam!<br>
Válaszúthoz értem,<br>
de elhagytam magam...
</p>

<p>
Hogy egyedül döntsek,<br>
mért kívánod tőlem?!<br>
Hogy vagy képes ennyit<br>
kinézni belőlem?!...
</p>

<p>
Már próbáltam &ndash; hidd el &ndash;,<br>
de az nem ment nekem,<br>
s a szerelem kardja<br>
döfte át a lelkem!...
</p>

<p>
E kínzó gyötrelem<br>
tán örökre altat,<br>
hiszen elmém néma,<br>
és a szívem hallgat...
</p>

<p>
A szívem hallgat.
</p>

