<p>
Szél szavára jót nevető,<br>
lágy őszi tájra meredő,<br>
elnémuló semmiségben<br>
susogva vetkező erdő.
</p>

<p>
Nyirkos fák törzse közt (távol)<br>
hullámzó, fehér ködfátyol &ndash;<br>
ezüstként csillámló harmat<br>
csöppenése hajló ágról.
</p>

<p>
Tócsa-merev, puha percek,<br>
halkan hulló esőcseppek;<br>
bólogató sárga lombok,<br>
kergetőző kis levelek.
</p>

<p>
A kék égbolt tisztasága<br>
most sok szürke felhő társa<br>
(s néhol egy-egy leselkedő,<br>
árnyéknyi fény ragyogása).
</p>

<p>
Hűs már a lég a völgybe’ lent &ndash;<br>
csak áldott béke, öröm-csend;<br>
házak fölött lágy füstcsóva,<br>
s lobogó tűz még odabent.
</p>

<p>
Álomra váró pihenés,<br>
lanyhuló, meleg szívverés,<br>
s ringatózó kis pelyhekkel<br>
tájat takaró hóesés.
</p>

