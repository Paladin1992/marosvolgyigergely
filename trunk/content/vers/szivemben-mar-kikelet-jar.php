<p>
Szívemben már kikelet jár,<br>
arcomhoz ér lágy napsugár,<br>
s kihajtanak tarka álmok:<br>
ibolyák és tulipánok.
</p>

<p>
Menetelnek kis bogarak,<br>
fű csiklandoz talpam alatt;<br>
kertem fáit körbemetszem,<br>
zöld pázsiton hanyatt fekszem.
</p>

<p>
Nyitnikéktől zsong az erdő,<br>
rám nevet sok pufók felhő,<br>
s míg mezőillat útra kel,<br>
egy patak sóhajt: énekel.
</p>

<p>
Fészket raknak emlékképek,<br>
nyüzsgő méhek döngicsélnek;<br>
rügyes fákon virághabok,<br>
időm lassan eloldalog...
</p>

<p>
Szép lugasban üldögélek &ndash;<br>
pihen most a szív és lélek,<br>
s itt cikáznak lepke-vágyak:<br>
tudják már, hogy nagyon várlak!
</p>

