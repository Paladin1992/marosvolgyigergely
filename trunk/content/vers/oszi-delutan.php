<p>
Már a délután perceit<br>
számlálta az óra,<br>
míg te kint, a hintaágyon<br>
tértél nyugovóra.
</p>

<p>
Kimentem a verandára<br>
&ndash; csak osonva, csendben &ndash;,<br>
és óvatosan leültem<br>
ide, veled szemben.
</p>

<p>
Gondoltam, netán olvasok<br>
pár oldalt e könyvben:<br>
ehelyett mereven néztem<br>
arcod, alvás közben.
</p>

<p>
Eltűnődtem, mi húzhatta<br>
szép mosolyra ajkad &ndash;<br>
Tán egy szebb világ jelei<br>
mutatkoztak rajtad?
</p>

<p>
Felhőkre könyökölt a nap,<br>
fény lepte be arcod;<br>
s szép álmod tengerén úszva<br>
kerested a partot. 
</p>

<p>
A friss őszi szellő lágyan<br>
simogatta tested,<br>
míg az avarlepte kertben<br>
virágillat terjedt.
</p>

<p>
S te csak némán szenderegtél,<br>
nem zavart meg semmi:<br>
nekem így is megtisztelő<br>
sorsod része lenni.
</p>

<p>
Szavak nélkül, szívem súgja<br>
most, hogy szeret téged &ndash;<br>
s az az igaz szeretet, ha<br>
álmodban is érzed...
</p>

