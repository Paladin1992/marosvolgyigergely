<h3>Hóvirág</h3>

<p>
Nem érdekel a világ &ndash;<br>
én hóvirág<br>
leszek!
</p>

<p>
Hiába nyom e fehér<br>
takaró súlya, megér<br>
annyit a fáradozás,<br>
az álmodozás,<br>
hogy utat törjek fölfelé:<br>
a fény,<br>
a fény,<br>
a fény felé...!
</p>

<p>
Hiába a fagyos hideg &ndash;<br>
reményt hozok mindenkinek!
</p>

<p>
Nem érdekel,<br>
hogy pici vagyok,<br>
ha letapostatok &ndash;<br>
mert az élet hevít legbelül<br>
s a hit, hogy majd felül<br>
minden szép lesz:<br>
hát felnyúlok a magas éghez!
</p>

<p>
Én a tavaszt hirdetem:<br>
az elmúlás végét,<br>
mert a teremtés zenéjét<br>
hallom szüntelen...<br>
Halljátok e gyönyörű dalt?<br>
A mindenség dalol...<br>
Halljátok e hívó szót?<br>
Az élet hangja zeng...
</p>

<p>
Szinte már érzem<br>
a nap örömteli melegét,<br>
látom a felhők lágy egét,<br>
tarka illatok ringását<br>
a szélben,<br>
hallom az erdő<br>
zöldellő<br>
zúgását,<br>
ahogy megindulnak<br>
álmos bogarak,<br>
ahogy az olvadó, friss patak<br>
régi medrét öleli,<br>
s rügyekbe bújva<br>
minden újra<br>
élettel lesz teli... 
</p>

<p>
Nem érdekel<br>
a feldúlt,<br>
korhadó múlt,<br>
ígérete hazug télnek,<br>
az eltemetett emlékek,<br>
beváltatlan, régi álmok,<br>
az sem, hogyha utamba álltok:<br>
nem érdekel már semmi! &ndash;<br>
Csak hóvirág szeretnék lenni...
</p>

<p>
Nem kérek mást,<br>
csak egy talpalatnyi eget,<br>
hol kibújva a fényre,<br>
lehajthatom fejemet,<br>
mert csak a szerény<br>
erő lehet igaz erény...
</p>

<p>
S a hervadás, ha beköszönt,<br>
majd kinyílok a szívetekben,<br>
és továbbviszik, mit elkezdtem<br>
&ndash; mit áldásként hagyok rátok &ndash;,<br>
a szépség örök<br>
hírnökei: fényígérő virágok!
</p>

