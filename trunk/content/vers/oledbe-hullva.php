<h1>Öledbe hullva</h1>

<p>
Mint tengerszilaj áhítat,<br>
pillantásod átitat;<br>
sóhajodtól boldog vagyok,<br>
karjaidban elhamvadok;<br>
felperzselnek szelíd vágyak:<br>
szívemből nőtt gyönyör-ágak;<br>
lüktetnek e sóvár kezek,<br>
csak hogy végre érintsenek;<br>
érted én már bármit adnék:<br>
úgy szeretlek &ndash; beléd halnék,<br>
s mert szüntelenül mar a kín:<br>
engedd hát, hogy megpihenjek<br>
ajkad édes partjain...
</p>

