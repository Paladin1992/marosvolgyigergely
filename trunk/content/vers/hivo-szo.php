<p>
Hajnalban még harmat vagyok,<br>
csöpp kis cseppem némán ragyog;<br>
percek alatt fátyol leszek:<br>
köddé tesznek levél-neszek.
</p>

<p>
Árnyék voltam: napfény lettem;<br>
nyájas tűzként ágat ettem;<br>
kövecskék közt meg nem álltam:<br>
hűs patakként folydogáltam.
</p>

<p>
Kis gyümölcsként aláhulltam,<br>
rögök közé jól elbújtam &ndash;<br>
kihajtottam felhők felett,<br>
ezüst könnyem hópihe lett...
</p>

<p>
Mint fodrozódó víztükör,<br>
remeg a lelkem, tündököl;<br>
sors feszül rá vitorlámra,<br>
s kihajózom új világra...
</p>

<p>*</p>

<p>
Elért a lét hívó szava:<br>
mély égboltnak csillag-tava,<br>
s egy isteni varázsige<br>
dalt komponált a semmibe.
</p>

<p>
Még most is hallom énekét,<br>
látom mosolygó, szép szemét:<br>
szárnya alatt szívem ébred &ndash;<br>
ha elméd nyugodt, megérted.
</p>

