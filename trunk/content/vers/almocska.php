<h3>Álmocska</h3>

<p>
Nézd, barátom,<br>
van egy álmom:<br>
fejből tudni minden verset,<br>
mi létezik e világon!
</p>

<p>
Így ha lélek-tárva időm töltöm<br>
&ndash; míg a szép sorokat számra öltöm &ndash;,<br>
majd gazdagabbá leszek én,<br>
mint bárki más e féltekén!
</p>

