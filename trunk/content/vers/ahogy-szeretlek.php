<h1>Ahogy szeretlek</h1>

<p>
Ahogy csókod megpihen ajkamon,<br>
ahogy kezem kúszik le hátadon,<br>
ahogy telhetetlen ölelsz engem,<br>
ahogy vad lángokat gyújtasz bennem,<br>
ahogy azt a szócskát súgod halkan,<br>
ahogy megigéz egy régi dallam,<br>
ahogy benned leltem otthonomra,<br>
ahogy velem sétálsz kézen fogva,<br>
ahogy nincs titok a közös ágyban,<br>
ahogy perzselődünk tüzes vágyban,<br>
ahogy könnyeimet csak te érted,<br>
ahogy világ ellen vagyok vérted,<br>
s ahogy két szív dobogja szüntelen:<br>
ez a boldogság &ndash; ez a szerelem!
</p>

