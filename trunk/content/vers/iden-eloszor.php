<h1>Idén először</h1>

<p>
Idén először<br>
kisütött a Nap,<br>
s mint szél a bús fákat,<br>
gyengén karomba fontalak...
</p>

<p>
Idén először<br>
tiszta volt az ég:<br>
ilyen fényesen az<br>
éjszaka nem ragyogott rég...
</p>

<p>
Idén először<br>
szerelmes lettem,<br>
s íme, most bevallom:<br>
neked adnám szívem, lelkem!
</p>

<p class="writing-info">
Nagymaros,<br>
2012. január 3-4.
</p>