<h1>Nincs rá szó<br>
<span class="subtitle">(T. B. részére)</span></h1>

<p>
Nincs rá szó,<br>
hogy leírjam,<br>
mekkora hibát követtem el,<br>
hogy játszottam érzéseiddel!
</p>

<p>
Mentségem sincs<br>
a sok döntésre,<br>
mellyel a hibákat halmoztam,<br>
s amiért magamra maradtam.
</p>

<p>
Csak kérésem van<br>
Hozzád, Te drága kincs,<br>
mi tán enyhíti e fájdalmat:<br>
Bocsáss meg nekem, s adj irgalmat!
</p>

<p>
Végül hát bevallom:<br>
Te mindig fontos leszel...<br>
Csak még egyszer forrón ölelj meg,<br>
hogy érezd... mennyire szeretlek!
</p>

<p class="writing-info">
Vác,<br>
2012. február 2.
</p>