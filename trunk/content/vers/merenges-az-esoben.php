<h3>Merengés az esőben</h3>

<p>
Szürke az ég,<br>
nincs kedve most kékleni,<br>
fáradt a nap fényleni,<br>
s fülledt a lég.
</p>

<p>
Koponyámban<br>
suttogva zörög a múlt;<br>
a szép darabokra hullt<br>
már korábban...
</p>

<p>
A bűn magát<br>
most lelkemben keresi,<br>
(talán csak vágyam lesi)<br>
s zengi dalát.
</p>

<p>
Tenger emlék:<br>
szívem könnyel önti el,<br>
már az Isten se figyel &ndash;<br>
s nincs menedék!...
</p>

