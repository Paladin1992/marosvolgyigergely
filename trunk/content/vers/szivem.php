<h1>Szívem</h1>

<p>
Puha csend ringatja lelkem,<br>
pihenget a semmi ölén;<br>
szívem másba elültetem,<br>
hogy öröm-fa nőjön fölém:
</p>

<p>
minden zsenge aranyágán<br>
gyümölcsözzön a kegyelem,<br>
s levelei közül tarkán<br>
hulljon le majd a szerelem.
</p>

<p class="writing-info">
Nagymaros,<br>
2012. március 12-15.
</p>