<h1>Új kezdet előtt<br>
<span class="subtitle">(szilveszterkor)</span></h1>

<p>
Rossz volt nekem ez az év,<br>
hátha jobb lesz majd az új:<br>
nem marad meg semmi sem &ndash;<br>
változás az örök úr.<br>
Csalódás a szerelem,<br>
aki józan, bolondul;<br>
szeretni csak az ügyes,<br>
ki maga is boldogul!
</p>

<p class="writing-info">
Nagymaros,<br>
2013. december 31.
