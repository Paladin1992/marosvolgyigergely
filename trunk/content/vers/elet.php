<h3>Élet</h3>

<p>
Sokan élik, miről<br>
én csak gondolkodom,<br>
lehetnék boldog, ám<br>
egyre több a gondom.
</p>

<p>
Pedig lehet, ez mind<br>
csupán képzelt talány,<br>
s ezért oszt szerepet<br>
rám sokszor a magány.
</p>

<p>
S kinek jobb közülünk?<br>
Olyan, ki tudja, nincs:<br>
másnak élni csoda,<br>
nékem csodája kincs!
</p>

