<h3>Örök perc<br>
<span class="subtitle">&ndash; mert az eskütétel csak egy perc, mégis egy életre szól...</span></h3>

<p>
Eljött a perc, mit régen vártunk,<br>
szép jövőnk útján együtt járunk:<br>
két külön szív, de közös lélek, <br>
te értem és én érted élek.
</p>

<p>
Nincs szebb csillag nálad az égen,<br>
társadnak lenni örök érdem &ndash;<br>
rabjává tett megértő szemed,<br>
mind, mit lehet, megadom neked.
</p>

<p>
E perctől te vagy az egyetlen,<br>
a te oldalad az én helyem;<br>
mikor a sors bánattal befed,<br>
majd ölellek és fogom kezed.
</p>

<p>
Imáimra te vagy a válasz,<br>
jóban és rosszban igaz támasz;<br>
legyen létünk nehéz vagy könnyebb:<br>
én ott leszek, és törlöm könnyed.
</p>

<p>
Te vagy, kiben otthonra leltem,<br>
nyájas kis tűzként lobogsz bennem:<br>
a szív lágy kelyhét vágyunk töltse,<br>
s majd szerelmünknek lesz gyümölcse!
</p>

<p>
Lehunyt szemmel is arcod látom,<br>
tarkára fested minden álmom;<br>
veled ébredni mindig öröm,<br>
s hogy rád találtam: megköszönöm!
</p>

<p>
Boldog évek küszöbén állva<br>
&ndash; egy új jövő kezdetét várva &ndash;<br>
most megteszem, mire Isten kért:<br>
és kezemet nyújtom szívedért!
</p>

<p>
Míg a hűség lángja tisztán ég,<br>
míg áldást lehel a magas ég,<br>
s míg örök alkony fénye ébred:<br>
teljes szívből szeretlek téged!
</p>

