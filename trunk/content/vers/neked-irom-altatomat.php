<h1>Neked írom altatómat...</h1>

<p>
Neked írom altatómat,<br>
puha bársony megcirógat,<br>
ölembe hullsz hűs pamlagon,<br>
pici szíved betakarom.
</p>

<p>
Hívnak már a csillagok,<br>
megjöttek az angyalok,<br>
hunyd le szépen kis szemed,<br>
dúdoljon az éj neked.
</p>

<p>
Álmaidban veled leszek,<br>
lágy szellővel énekelek,<br>
tücskök adnak tarka mesét:<br>
csókom lesz az igaz pecsét...
</p>

