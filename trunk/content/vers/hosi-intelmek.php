<h3>Hősi intelmek</h3>

<p>
Láss, hogy meglássák az igaz fényt;<br>
Hass, hogy érezzék az új reményt;<br>
Bízz erődben, ha csatába mész;<br>
Higgy magadban, legyél harcra kész;<br>
Ártatlant soha ne ostorozz;<br>
Szívekből haragot elorozz;<br>
Tisztítsd meg lelked minden rossztól;<br>
Ne félj sötéttől, se gonosztól;<br>
Isten segít, míg a fényben lépsz;<br>
Halj meg másért, és örökké élsz!
</p>

