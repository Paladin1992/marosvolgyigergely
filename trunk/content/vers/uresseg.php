<h1>Üresség</h1>

<p>
Nem létező, fájó percben<br>
ülök magam kis ketrecben,<br>
s mert nem jöttek értem mások,<br>
körbevesznek kemény rácsok...<br>
</p>

<p>
S míg kimondatlan szavak itt<br>
huzatként járnak, hamvait<br>
keresvén egy gondolatnak:<br>
áthat melege a fagynak.
</p>

<p>
Hirtelen felkiált a csend,<br>
s hogy kiürül e tág űr bent,<br>
lelkemben egy gyermek lázad,<br>
s könnye hull halkan: a bánat.
</p>

<p>
Minden szín csak szürke öröm:<br>
csupán a perceket ölöm,<br>
míg a percek engem ölnek,<br>
s reményeim romba dőlnek.
</p>

<p>
Bár mindent kitölt a semmi,<br>
semmivé akarja tenni<br>
létem egy sötét tűz pora:<br>
a zord idő halálsora...
</p>

<p>
Csak egy sóhaj mered rám most &ndash;<br>
néz bennem egy kihalt várost,<br>
hol magányfalak közt járnak<br>
álmai egy szebb világnak.
</p>

