<h1>A Dunánál (jött rám...)</h1>

<p>
A rakodópart alsó kövén ültem,<br>
néztem, hogy úszik el egy búváruszony;<br>
alig hallottam, görcsömbe merülten,<br>
hogy’ röhögnek rajtam hajón és buszon.
</p>

<p>
Volt olyan, ki csak fényképezett némán,<br>
míg más a nevetéstől hasát fogta &ndash;<br>
nos, hasam fogtam én is fájón, bénán,<br>
ahogy porban fetrengtem haldokolva.
</p>

<p>
Oh, jaj, pedig otthon még nagy volt a szám,<br>
s mondtam: ”Nincs alku! Én hagy legyek teli!”<br>
Ebédre palacsintát csinált anyám,<br>
azt meg ugye minden gyerek szereti...
</p>

<p>
Majd ősz hajú mamám etetett engem:<br>
„Egyél csak! Nekem nősz nagyra, kis Balázs!”<br>
S szilvalekvárt, birsalmát kellett ennem!...<br>
(Te jó ég, mit rejthet még spájz és padlás?!)
</p>

<p>
S folytatá mama: „Ha az aszalással<br>
kész leszek, kaphatsz szárított gyümölcsöt!...”<br>
S én az utcára szöktem zokogással<br>
&ndash; ekkor még nem éreztem hasi görcsöt.
</p>

<p>
Loholva értem ki a Duna-partra,<br>
hogy ne tömjön a mama, mint a libát &ndash;<br>
bár tény, hogy vékony vagyok, mint a szalma,<br>
a módszerében érzek némi hibát.
</p>

<p>
Dióbélnyi gyomrom nem bírta, morgott,<br>
s éreztem, hogy fogyó időm oly kevés!<br>
Restellem e nem fülbe való dolgot,<br>
de mondjuk ki nyíltan: ez hasmenés...
</p>

<p>
Cikázó lepkeként járt tekintetem<br>
(én, naiv lélek, megváltásra vártam),<br>
hiún reménykedve égre meredtem...<br>
s egy feketerigó tojta le vállam.
</p>

<p>
Függöny, taps: az előadásnak vége,<br>
sokféle ember jön ki a színházból,<br>
özönlenek &ndash; „Áh, sose jutok én be!”<br>
Pedig közel a cél... és mégis távol!
</p>

<p>
Végül a remény hangja hívott engem,<br>
és egy kávéház mosdójába (végre!)<br>
elképesztő gyorsasággal bementem,<br>
s ott rám mosolygott a megváltó béke...
</p>

