<h1>Mily távolságot kell még megtennem...</h1>

<p>
Mily távolságot kell még megtennem,<br>
&ndash; Oh, kérve kérlek, mondd el nekem &ndash;,<br>
Hogy megint csak te járhass fejemben,<br>
Csakis te &ndash; senki más &ndash;, kedvesem?!
</p>

<p>
Hisz nem tudlak nélkülözni téged,<br>
Mert szerelmed vad lánggal éget;<br>
Ne légy oly kegyetlen, mint az élet,<br>
Mely nem hagy soha engem véled!
</p>

<p>
Tudom, csak egy lépést kéne tennem,<br>
S máris hiányoznál, Életem,<br>
Ki szép vörös rózsa vagy kertemben:<br>
Legszebb, mit láttam életemben!
</p>

<p>
Ha távol is járok a világban,<br>
Hol csak bú s magány útitársam,<br>
Tüzes érzelmeink hiányában,<br>
Én biz’ elvesznék a homályban.
</p>

<p>
Térjek haza? Csak szavadba kerül!<br>
Meglásd, csókom újfent rád vetül,<br>
S ifjú szerelmünk mindinkább hevül:<br>
Egymásé leszünk mindenestül!
</p>

