<p>
Dunai széllel jár most a napfény táncot:<br>
fényjátékuk csillog a kristályos vízen,<br>
s néhol bús jégtáblák szelnek bele ráncot,<br>
miképp szerelem szokta vagdosni szívem.
</p>

<p>
Eme kopasz fákkal gyötört havas tájba<br>
e téli kép visz csak halovány életet;<br>
a napfény is küzd még, de végül belátja:<br>
még hagyni kell aludni a természetet.
</p>

<p>
Ahogy tekintetem a folyóra mered,<br>
s én csak lesem annak minden ragyogását:<br>
szinte érzem vállamon enyhítő kezed,<br>
mint a tavasz reményteli csillogását...
</p>

<p>
A napot fönn már csak egy lehelet tartja;<br>
a Duna selyemszerű, nyugodt felszínét<br>
madarak árnya, néha uszály zaklatja,<br>
mint mikor tengernyi bársonyt tőr hasít szét...
</p>

<p>
A kétkedésnek homálya lepte arcom,<br>
s eltűnődtem: van-e még, ki szívből szeret?...<br>
De aztán megláttalak a folyóparton,<br>
s bennem már tarkán virágzik a kikelet!
</p>

