<h1>Ne bántsd a költőt!</h1>

<p>
Ne bántsd a költőt!<br>
<span style="display:inline-block; padding-left:20px;">Hisz’ nem ő tehet róla,</span><br>
<span class="indented-line by20">hogy fáj neki az élet,</span><br>
<span class="indented-line by20">s mert versbe csak azt rója,</span><br>
<span class="indented-line by20">mi lélekben övé lett.</span><br>
</p>

<p>
Isten küldte őt:<br>
<span class="indented-line by20">hogy írjon arról, mi szép,</span><br>
<span class="indented-line by20">s mint örök nyíló virág,</span><br>
<span class="indented-line by20">óvja, mit lehet, míg ép:</span><br>
<span class="indented-line by20">s tán feléled a világ...</span><br>
</p>

<p>
Becsülhetnéd őt:<br>
<span class="indented-line by20">derűt és borút dalol,</span><br>
<span class="indented-line by20">szóval küzd a világgal,</span><br>
<span class="indented-line by20">de mikor kell: átkarol</span><br>
<span class="indented-line by20">egy menedéknyi lánggal.</span><br>
</p>

<p>
S ha nem becsülöd?...<br>
<span class="indented-line by20">Hűs és léha lesz lelked,</span><br>
<span class="indented-line by20">melybe nem hoz már újra</span><br>
<span class="indented-line by20">csillogó reményt neked</span><br>
<span class="indented-line by20">a költő koszorúja.</span><br>
</p>

