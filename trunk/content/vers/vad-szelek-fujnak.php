<p>
Szomorú dalt süvít a szél,<br>
érzi már, hogy útra kelsz vele,<br>
s bár bennem még a remény él,<br>
görnyedten hull a fák levele...
</p>

<p>
Alig még, hogy rád találtam,<br>
a sors keze máris szétszakít,<br>
s hiába, hogy annyit vártam:<br>
a türelem ismét elvakít...
</p>

<p>
Már itt a vonat &ndash; menned kell:<br>
lehelj rám csókot és fogd kezem,<br>
csak fonj karodba lelkeddel,<br>
s én szíved szívembe elteszem...
</p>

<p>
Eljött a sors kettős kínja:<br>
külön a test, együtt a lélek;<br>
futok, ahogy lábam bírja:<br>
nyúlok utánad, s el nem érlek!...
</p>

<p>
Összefolynak lágy vonalak:<br>
messzi tájak ölelnek téged;<br>
csak állok itt, mint árnyalak &ndash;<br>
szemem utánad hosszan réved...
</p>

<p>
Bár vad szelek fújnak-tépnek,<br>
sóhajod hevít még tüzesen,<br>
s esküszöm a magas égnek:<br>
visszavárlak téged, kedvesem!...
</p>

