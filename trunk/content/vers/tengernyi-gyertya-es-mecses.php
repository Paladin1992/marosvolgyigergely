<h3>Tengernyi gyertya és mécses</h3>

<p>
Mennyi szép csillag tündököl<br>
az éji csendben odafenn!...<br>
S megannyi puszta sír hallgat<br>
a temetőben idelenn.
</p>

<p>
Komor kőtömbök és fejfák &ndash;<br>
némelyik szép, drága márvány:<br>
mind csupa megkövült könnycsepp,<br>
gyászosan, mereven állván.
</p>

<p>
Akik egykor köztünk éltek,<br>
most vésett nevek és számok:<br>
elporladt emlékek, testek;<br>
kialudt szívek és lángok.
</p>

<p>
Isten keze által ők is<br>
e földi porból születtek,<br>
s mi magunk is azzá leszünk,<br>
ha majd egyszer eltemetnek...
</p>

<p>
Sokan csak most eszmélnek rá:<br>
tisztelni kellett volna Őt!<br>
S csak most érzik elveszettnek<br>
a nem Vele töltött időt!
</p>

<p>
„Istenem! Mit nem adnék, ha<br>
most ideülhetne mellém!<br>
Ha újra csak vele lennék,<br>
s gyöngéden átölelhetném...” 
</p>

<p>
Mereven és szavak nélkül<br>
szólunk néma emlékéhez;<br>
ezüst könny gyűlik szemünkben,<br>
s mind lehull majd sírkövéhez...
</p>

<p>
Verőfényes már az égbolt:<br>
fentről néznek a csillagok,<br>
de az Értük felgyúlt lángok<br>
fénye mégis szebben ragyog!
</p>

<p>
A rideg sírok kövein<br>
áthat a gyertyák melege,<br>
s feléleszti hamvaikat<br>
a családok szeretete!...
</p>

<p>
Ki fontos neked, öleld át,<br>
hadd érezze szereteted!<br>
S ha már nem lesz rá alkalmad,<br>
bánhatod, hogy nem tetted meg!
</p>

<p>
Most tegyél jót másokkal, hogy<br>
mindig szeretetben élhess:<br>
s egyszer Érted is lobogjon<br>
tengernyi gyertya és mécses!
</p>

