<h1>Kései búcsú<br>
<span class="subtitle">(szonett Sz. A. részére)</span></h1>

<p>
Mint jót akaró kisgyerek,<br>
ki ha rosszat tesz, nem tanul<br>
belőle, s könnye csak pereg,<br>
mert nem érti, miért lapul
</p>

<p>
minden szép szerelem mögött<br>
annyi kíntól izzó óra,<br>
mely éget belül szerfölött,<br>
hogy ember nem számít jóra &ndash;
</p>

<p>
úgy álltam csendben, szótlanul,<br>
szabadon, de mégis rabul,<br>
tudván: el kell engedjelek
</p>

<p>
fagynak és őszi szeleknek;<br>
s bár a lét engem mar vadul:<br>
jó volt veled &ndash; szerettelek.
</p>

<p class="writing-info">
Nagymaros,<br>
2013. december 31.
</p>

