<h1>Átkarolt az ősz...</h1>

<p>
Szeptember &ndash; így szólt a naptár
</p>

<p>
kapunknál vöröslik a madárberkenye<br>
s még levenduláról álmodik a rét
</p>

<p>
ablakomból kinézve látom<br>
hogy’ sárgulnak a nyírfa levelei
</p>

<p>
domboldalon könyököl a nap<br>
felhőket sepreget a szél odafent
</p>

<p>
állni látszék a vén Duna tükre<br>
fáradt hajók súlya alatt
</p>

<p>
elnémul a fövenyes part<br>
horgonyt vet az emlékezet
</p>

<p>
még utoljára megfürdenek<br>
s álmatagon csillapulnak<br>
ifjú vágyak
</p>

<p>
a változás küszöbén állunk<br>
most &ndash; és mindörökké
</p>

<p>
egy levélnyi pillanat<br>
elindult az elmúlás felé<br>
s színeivel dalolászva<br>
átkarolt az ősz...
</p>

