<h3>Ha karácsonyfa dísze lennék...</h3>

<p>
Ha karácsonyfa dísze lennék,<br>
zsenge ágon megpihennék,<br>
s míg körbejárna vidám ének,<br>
szaloncukrok ölelnének.
</p>

<p>
Míg megjönne sok szép ajándék,<br>
csengőszóra lengve várnék,<br>
s ha arcom fényes tükör lenne:<br>
szép világot látnál benne.
</p>

<p>
Ha kis dobozba el is tesznek,<br>
emlékeim szépek lesznek,<br>
mert ott ragyogtam szívük táján:<br>
szeretetnek fenyőfáján!
</p>

