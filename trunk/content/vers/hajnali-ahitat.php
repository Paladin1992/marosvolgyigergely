<p>
Még fénylik a hold odafent,<br>
de búcsúznak a csillagok,<br>
s bár ciripel a tücsök lent,<br>
már indulnak az angyalok.
</p>

<p>
Lám, ébredez’ a hűs szellő<br>
&ndash; s míg újra csobog patak, ér &ndash;,<br>
fakulva bomlik sok felhő,<br>
s a tündérsereg hazatér.
</p>

<p>
Szemét nyitja már a hajnal,<br>
nap nyújtózik a dombokon:<br>
ismerkedik ezer hanggal,<br>
s aranyat önt a lombokon.
</p>

<p>
Világosság! &ndash; szól az erdő;<br>
(múlnak már az árnyak végre)<br>
s ahogy ezer karja megnő,<br>
harmatot lehel a rétre.
</p>

<p>
Köd teríti halvány fátylát,<br>
áttör a fény a fák között,<br>
bontogatván arany szárnyát,<br>
fehér párába öltözött.
</p>

<p>
S ébredő szívünk megtelik<br>
nap nap után a reménnyel,<br>
ahogy lágyan szeretkezik<br>
a sötétség a napfénnyel...
</p>

