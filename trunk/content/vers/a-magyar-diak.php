<div class="subtitle">(Avagy a mai fiatalokat kigúnyoló vers<br>
Petőfi Sándor „A magyar nemes” c. verse mintájára)</div>

<p>
Hajvasaló, szakadt gatya,<br>
Szobámat a bűz kimarja;<br>
Hifiből már zeng a rock!<br>
Én magyar diák vagyok!
</p>

<p>
Lustálkodás csak az élet;<br>
Nem tanulok, mert henyélek;<br>
A stréberé a dolog.<br>
Én magyar diák vagyok!
</p>

<p>
Verdával szelem az utat,<br>
Ma már nem menő a vonat;<br>
Csak nem járhatok gyalog!<br>
Én magyar diák vagyok!
</p>

<p>
Tán satnya könyvmolyként éljek?<br>
Uncsik már a költemények!<br>
Nem írok, nem olvasok:<br>
Én magyar diák vagyok!
</p>

<p>
Van, igaz, egy tudományom,<br>
Ebben párom ritkán látom:<br>
Aludni, azt jól tudok!<br>
Én magyar diák vagyok!
</p>

<p>
Cigizek s alkoholt iszok<br>
Haverokkal jót bulizok.<br>
Szüleimtől van pénz, sok.<br>
Én magyar diák vagyok!
</p>

<p>
Szexigény már korán érik,<br>
Az egót kilóba’ mérik,<br>
Nagymellű’k a jó csajok!<br>
Én magyar diák vagyok!
</p>

<p>
Jövő nélkül, kicsi házban,<br>
Éltemet, ha elkártyáztam:<br>
Büszkén leszünk magyarok!<br>
Én magyar diák vagyok!
</p>

