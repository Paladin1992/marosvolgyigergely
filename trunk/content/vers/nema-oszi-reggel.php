<p>
Gyűrt paplan a meleg ágyon:<br>
félbehagyott, édes álom;
</p>

<p>
lassan hűlő, nyomott párnák,<br>
fény-lehunyó utcalámpák;
</p>

<p>
pirítós és meleg tea &ndash;<br>
függöny előtt orchidea;
</p>

<p>
üveg tükrén hűvös pára:<br>
rajzom húzza szép simára;
</p>

<p>
halk léptekben úszó sétány,<br>
tócsa remeg rajta némán;
</p>

<p>
homályba vész egy árnyalak &ndash;<br>
még szótlanok a madarak...
</p>

<p>
deresre görnyedt fűszálak,<br>
mozdulatlan, nyirkos ágak;
</p>

<p>
kerítésen kláris-vonal:<br>
gyöngyökbe bújt, lágy pókfonal;
</p>

<p>
letaposott levél-minta...<br>
magányosan áll a hinta;
</p>

<p>
kimondatlan szavak íze:<br>
ködfátyolnak sóhaj-dísze &ndash;
</p>

<p>
elmémet a csend járja át,<br>
hallgatom a város zaját...
</p>

<p>
s vagyok én: ki csak ténfereg,<br>
és vannak ők: az emberek,
</p>

<p>
kik rohannak, míg én állok &ndash;<br>
nem értik, hogy mit is várok...
</p>

<p>
de itt jön már, és rám köszön:<br>
mosolyt fakaszt e csöpp öröm &ndash;
</p>

<p>
s tág szemekkel, reménytelten,<br>
nézzük egymást itt, mi ketten:
</p>

<p>
csak én<br>
s a fény...
</p>

