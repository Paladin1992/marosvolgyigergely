<p>
Ha újra hull a fehér hó,<br>
tiszta lombja lesz a fáknak,<br>
lenyugszik a szilaj tenger,<br>
s pihennek majd mind a vágyak.
</p>

<p>
Elnyújtózna háztetőkön,<br>
minden kis hang elenyészne;<br>
hullnának a pici pelyhek:<br>
nyitott szem se venné észre.
</p>

<p>
Lefektetne zöld füveket,<br>
fehéredne rét és búza,<br>
betelnének sötét vermek,<br>
s minden hegyre paplant húzna.
</p>

<p>
Elvesznének rideg formák:<br>
maradna, mi szép és kerek;<br>
hallanánk a belső hangot,<br>
s játszanánk, mint jó kisgyerek.
</p>

<p>
E simogató, lágy lepel<br>
megóvná az emlékeket,<br>
békét hozna bús szíveknek,<br>
és lemosná a könnyeket.
</p>

<p>
Minden, mi rossz, jégbe fagyna:<br>
darabokra összetörne &ndash;<br>
s végre már az ártó szavak<br>
eltűnnének örök csöndbe.
</p>

<p>
Gőg és harag lepihenne,<br>
trónfosztva állna mind a pénz,<br>
s míg békejobbot nyújtanánk,<br>
ledobná a fegyvert a kéz.
</p>

<p>
Megállnának múló percek,<br>
fénnyé válna minden árnyék;<br>
puha gyémánt tündökölne:<br>
világgá nőtt, néma tájkép.
</p>

<p>
És jönne tavasz, jönne nyár<br>
&ndash; és nem múlna e fehérség:<br>
mert kivárná az olvadás,<br>
míg utat tör a reménység...
</p>

<p>
Maradna, ki gyógyít, épít,<br>
ki lelkét hordja tenyerén &ndash;<br>
hadd lobogjon a szeretet<br>
lángja mindenki tűzhelyén!
</p>

