<p>
Ismersz &ndash; vagyis te úgy hiszed,<br>
mert tudod a nevem régen;<br>
de most szívem épp elviszed,<br>
s szenvedek minden emléken.
</p>

<p>
Bőrönddel állok kapudban,<br>
ablakod nézem mereven,<br>
s amíg fuldoklom a múltban:<br>
bánat csurog az ereszen.
</p>

<p>
Volt, hogy izgatottan lested,<br>
ki is áll éppen idelenn,<br>
s kiben a társat kerested:<br>
most közönséges idegen.
</p>

<p>
Jöttek a fájó reggelek:<br>
kínba préselt a mennyezet;<br>
csak múltak a napok, hetek...<br>
s egyikünk mindig könnyezett.
</p>

<p>
Próbálok rá okot lelni,<br>
hogy egy kis lépést se tegyek,<br>
s már most nehéz elviselni,<br>
hogy eztán ne veled legyek!...
</p>

<p>
A hold még néha rám tekint,<br>
lelkem az ürességre kész:<br>
s vad tengerre vágynék megint,<br>
mint egy hajótlan tengerész.
</p>

<p>
Csak a hulló esőt érzem,<br>
mely már lassan bőrig áztat,<br>
bár több égő sebből vérzem,<br>
a szívem még mindig lázad.
</p>

<p>
Csak téged akar a lelkem,<br>
s még él bennem egy kis remény...<br>
de most oly erőtlen lettem,<br>
mint a halovány utcafény.
</p>

<p>
Bús szíved küszöbén állok &ndash;<br>
immár indulásra készen;<br>
életedből kisétálok...<br>
lassan lépdelve... egészen.
</p>

