<h1>Cseppnyi félelem</h1>

<p>
Szomorú az égbolt,<br>
potyognak könnyei:<br>
lehűtik az utcát<br>
ezüstös csöppjei.
</p>

<p>
Remeg a kis tócsa,<br>
gőzölög a járda &ndash;<br>
forró ölelésre<br>
ki az, aki vár ma?...
</p>

<p>
Kitárt karjaimmal<br>
meredek az égre:<br>
válasz nélkül állok<br>
itt &ndash; egyedül... félve...
</p>

