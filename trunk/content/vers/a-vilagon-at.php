<h3>A világon át</h3>

<p>
Ha kéred,<br>
dolgaimat félre téve,<br>
olthatatlan vágytól égve,<br>
szép álmomból felriadva<br>
útra kelnék lovat hajtva,<br>
s átgázolnék minden erdőn,<br>
utaznék egy sebes felhőn,<br>
szikráznék a csillagokkal,<br>
hadakoznék villámokkal,<br>
úsznék tajtékzó tengeren,<br>
kúsznék szilánkos földeken,<br>
leküzdenék völgyet-hegyet,<br>
eltipornék tüzet, jeget,<br>
s mint aki nem leli honát:<br>
rohannék a világon át &ndash;<br>
Tehozzád!
</p>

<p class="writing-info">
Veszprém,<br>
2013. január 22.
</p>