<h3>Ballagó évek<br>
<span class="subtitle">A nagymarosi Kittenberger Kálmán Általános és Művészeti Iskola<br>
alapításának 100. évfordulója alkalmából (1915-2015)</span></h3>

<p>
Mint a fürge, néma percek,<br>
úgy repültek el az évek,<br>
s visszaköszön gyermekkorom,<br>
ahogy iskolámba lépek.
</p>

<p>
Az idő mintha megállna,<br>
s átvonaglik ezer kérdés:<br>
hisz annyi minden változott,<br>
mégis: ugyanaz az érzés.
</p>

<p>
Elnyúlik a tágas udvar:<br>
csíny és jóság egy térfelen,<br>
s míg feltörnek az emlékek,<br>
kergetőzik múlt és jelen.
</p>

<p>
Krétás tábla, fali térkép,<br>
tanári asztal megmaradt,<br>
s a parketta, mi megkopott<br>
a sok osztály és pad alatt.
</p>

<p>
Folyosón visz furulyaszó,<br>
tenger fakó portrét látok,<br>
szemükben a remény tüzel:<br>
beváltatlan, gazdag álmok.
</p>

<p>
Bizony köztük vagyok én is:<br>
rám mered az önarcképem,<br>
ellátnám &ndash; ha lehetne &ndash; sok<br>
jó tanáccsal régi énem...!
</p>

<p>
Rázendül a harsány csengő,<br>
nyolc évnyi reggelt felidéz...<br>
Rájöttem, hogy nem is a kor:<br>
a tudás, ami megigéz!
</p>

<p>
Megismertünk betűt, számot<br>
&ndash; gyönyörű kis birodalom &ndash;,<br>
s jött a matek, földrajz, német,<br>
néptánc, nyelvtan, irodalom.
</p>

<p>
Rémlenek a felelések &ndash;<br>
(leckét néha reggel írtunk),<br>
a játékok, a napközik,<br>
s futkároztunk, amíg bírtunk!...
</p>

<p>
Itt tanultam, hogy az ember<br>
erős legyen, higgyen, várjon,<br>
s hogy tehetség és szorgalom<br>
kézen fogva kell, hogy járjon!
</p>

<p>
Annyi mindent hagytam itt, mik<br>
tovább élnek, mélyen bennem,<br>
s ami kellett ahhoz, hogy ily<br>
értékőrző Ember lettem!
</p>

<p>
Mert piciny mag a sok diák<br>
a tudás tarka kertjében,<br>
s a tanár az a kertész, ki<br>
gondozza őket serényen...
</p>

<p>
Életünk a legfőbb lecke,<br>
s a tanulás egy szép csoda;<br>
mert hű erény a műveltség<br>
&ndash; és alapja: az iskola.
</p>

