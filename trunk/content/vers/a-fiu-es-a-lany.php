<p>
Sétálni mentem egy messzi rétre,<br>
Hol friss levegőt szívhatok végre;<br>
Vígan nézem az azúrkék eget,<br>
S a szél odafönn felhőket kerget.
</p>

<p>
A déli napsugár forrón éget,<br>
Enyhülésért a patakhoz érek;<br>
Szép lassan nyelem hűsítő vizét,<br>
S élvezem minden cseppjének ízét.
</p>

<p>
Oly jól esik e mennyei zamat,<br>
Mint pázsitnak a reggeli harmat;<br>
S míg pezsdítő kortyaimat végzem,<br>
Testemnek vérét lanyhulni érzem.
</p>

<p>
Szomorú fűz áll ott a part mentén,<br>
Lombja alatt szép kislányt látok én...<br>
Haja barna, ruhája, mint selyem &ndash;<br>
Tán a képzeletem játszik velem?
</p>

<p>
A fűzfa ugyan lehet szomorú,<br>
De a lány arcán sehol semmi bú:<br>
Hisz vígan dúdolgat most kedvében,<br>
Míg én égek a vágynak hevében!
</p>

<p>
Járása gyöngéd, mint a lehelet,<br>
Szebbet kívánni nála nem lehet!...<br>
Most megáll: mosolyogva idenéz,<br>
Pillantása édes, akár a méz!
</p>

<p>
Lassan elindulunk egymás felé<br>
&ndash; S már tudom: szerelmes vagyok belé! &ndash;,<br>
Léptünk felgyorsult, szinte repültünk,<br>
S végül egymás karjába kerültünk.
</p>

<p>
Szoros ölelés volt, mégis gyengéd,<br>
Lelkembe fonta szelíd kis lelkét;<br>
Szívem mélyét boldogság lepte el:<br>
Csordultig lett igaz szerelemmel.
</p>

<p>
Kézen fogva szeljük a nagy rétet,<br>
Mintha e perc sosem érne véget...<br>
Millió szép virág hajlong felénk,<br>
S léptünkre reppennek tarka lepkék.
</p>

<p>
A nap már-már lassan lemenőben,<br>
Virágillat jár a hűs szellőben:<br>
Várjuk még, amit a pillanat hoz,<br>
Míg visszatérünk a patakparthoz.
</p>

<p>
A fűzfa lombja alá leülünk,<br>
S az alkony varázsába kerülünk:<br>
Utolsó sugarában a fénynek<br>
Lágy ajkaink forrón összeérnek...
</p>

<p>
Ma is csókolom őt álmaimban,<br>
Sodródva tengernyi vágyaimban...<br>
Mikor láthatom? &ndash; még most is talány,<br>
S mi lehetnénk: a fiú és a lány...
</p>

