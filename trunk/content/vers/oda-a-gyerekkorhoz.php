<h1>Óda a gyerekkorhoz</h1>

<p class="writing-quotation">
„Mikor gyermek valék,<br>
úgy szóltam, mint gyermek,<br>
úgy gondolkodtam, mint gyermek,<br>
úgy értettem, mint gyermek:<br>
minekutána pedig férfiúvá lettem,<br>
elhagytam a gyermekhez illő dolgokat.”<br>
(Pál apostol I. levele a Korinthusbeliekhez 13, 11)
</p>
 
<p>
<span class="verse-number">1</span><br>
De szeretnék gyerek lenni!<br>
Már csak ez az, mire vágyom! &ndash;<br>
Oh, miért kellett ily hamar<br>
ostoba felnőtté válnom?!
</p>

<p>
Az elbűvölő csodákat<br>
vissza már hiába várom;<br>
Mackóm se virraszt mellettem,<br>
hogy csendben őrizze álmom:
</p>

<p>
A polcról néz, s rég kérdeztem<br>
tőle: „Hogy vagy, kis barátom?”<br>
Ha bántottak, nem öleltem<br>
át, s most már hiába bánom...
</p>

<p>
<span class="verse-number">2</span><br>
Édesanyám rég letette<br>
a tarka mesekönyveket,<br>
s e kifakult valóságtól<br>
már nem szárnyal a képzelet...
</p>

<p>
Tengernyi építőkocka<br>
hever dobozok mélyein:<br>
eltemetett szivárványként,<br>
mint gyerekkori éveim...
</p>

<p>
Pedig lennék hős lovag, ki<br>
megmenti a szép lányokat;<br>
vagy varázslattal tépnék szét<br>
valóság szőtte láncokat!... 
</p>

<p>
<span class="verse-number">3</span><br>
Szétszórt s elveszett játékok,<br>
gyertek ide, bárhol vagytok!<br>
Másszatok elő szaporán,<br>
s körém gyűlve daloljatok!
</p>

<p>
Csodás lények a könyvekben,<br>
kik a képzeletben éltek:<br>
Keljetek fel álmotokból,<br>
s találjunk ki új meséket!
</p>

<p>
Zendüljön fel ezernyi szép,<br>
örömteli gyermek-dallam,<br>
és szóljon, míg a játéktól<br>
fáradtság vesz erőt rajtam...
</p>

<p>
S ha majd nagyon kimerültem,<br>
fonjon karjaiba anyám,<br>
s ringasson, míg le nem hunyom<br>
két szemem utolsó szaván...
</p>

<p>
<span class="verse-number">4</span><br>
Visszapillantok a múltba:<br>
s az emlékeket meglelem,<br>
ahogy az elmúlt éveket<br>
gyors homokszemként pergetem...
</p>

<p>
Most már csak arra várhatok<br>
vajmi kevés türelemmel,<br>
hogy jövőm megajándékoz<br>
engem saját gyermekemmel:
</p>

<p>
De mellette a játékban<br>
nem leszek majd más: csak vendég,<br>
pedig milyen jó lenne, ha<br>
újra kisgyerek lehetnék...!
</p>

