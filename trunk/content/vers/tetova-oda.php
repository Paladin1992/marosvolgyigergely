<h1>Tétova óda</h1>

<p>
Nem tudom, mit tegyek:<br>
Rajzoljalak, fesselek?<br>
Szívem dallamaként<br>
vigyázzak rád?<br>
Vagy inkább<br>
versnek soraiban<br>
papírra vesselek?
</p>

<p>
Ábrándjaimmal ölellek,<br>
mióta a nőt ismerem<br>
Benned, de nincs rá mód,<br>
hogy megörökítsem.
</p>

<p>
Lerajzolnám minden<br>
vonalad &ndash; gyöngéden<br>
simulva sejtjeidhez,<br>
majd kifesteném<br>
gyönyörű színeiddel,<br>
mik némán szólnak<br>
a bús szívekhez!...<br>
De az nem adná vissza lényed:<br>
e természetes szelíd kincset &ndash;<br>
varázslatos mosolyod...
</p>

<p>
S mi nem férne rá<br>
a papír tisztaságára,<br>
azok angyali szárnyaid<br>
és szereteted örökkön<br>
illatozó virága!
</p>

<p>
Ha dallal próbálkoznék:<br>
ahhoz hangjegyek sora<br>
is igen kevés volna!<br>
De ha lenne is Rád<br>
fény alkotta<br>
mennyei kotta:<br>
nem akadna hangszer,<br>
sem ember éneke,<br>
mely méltó lenne,<br>
hogy magára vegye<br>
szépséges dallamod<br>
finom rezdüléseit...
</p>

<p>
Tán írjalak versbe?<br>
Nem lehet!<br>
Ha az óceán minden<br>
vízcseppje is a legszebb<br>
szavak özönévé változna,<br>
még az sem volna elég,<br>
hogy akár megközelítse<br>
testi valód egészének<br>
örökzöld tökéletességét;<br>
s pennám se volna<br>
méltó, hogy kerekded betűid<br>
vonalát formálja!
</p>

<p>
S tudom, hogy hiába lenne<br>
csillámló tündér-könnyedre<br>
illő szavakat keresni<br>
a végtelenben merengve...
</p>

<p>
Tán nem is merném,<br>
hisz igéző szemedtől,<br>
e sugárzó szépségtől<br>
a törekvést felejteném...
</p>

<p>
Pedig ha lenne rá mód:<br>
éjt nappallá téve<br>
egyre csak kísérelném<br>
kimutatni a vágytól égve:<br>
mennyire tisztellek,<br>
szeretlek,<br>
ahogy halandó<br>
halandót nem szerethet &ndash;<br>
s már nem győzöm<br>
köszönni szereteted;<br>
a dobogni akaró,<br>
minden rosszat betakaró<br>
szívet, s e lelket,<br>
mely a kiapadhatatlan<br>
remény forrásvízéből,<br>
a múlt mesés mélyéből<br>
táplálkozik szakadatlan.
</p>

<p>
Beismerem: képtelenség<br>
megörökíteni, visszaadni,<br>
gyöngyházba foglalni,<br>
mit a jóság s tökéletesség<br>
egyedien megtestesíthet;<br>
mit Isten önnön képére,<br>
saját kezűleg teremtett,<br>
mit égi áldásnak vélek:<br>
Igen &ndash; Rólad beszélek!
</p>

<p>
(Oh, pedig mennyi csoda<br>
illatát ontod a világodra,<br>
míg pecsétes titkok hada<br>
nyílik meg bizalmadra!...)
</p>

<p>
Szeretném, ha tudnád:<br>
bárhová is visz a Sors<br>
vadul sodró szele &ndash;<br>
Szívem mindig<br>
szíveddel lesz tele,<br>
s lelkem is majd<br>
emlékek szárnyán suhan,<br>
mikor Rád gondolok... boldogan.
</p>

<p>
Csupán arra kérlek:<br>
&ndash; s ezt ne feledd &ndash;<br>
maradj mindig ilyennek,<br>
s mosolyoddal tedd<br>
szebbé napját mindenkinek!
</p>

<p>
Bár mindig oltárod tüzén égek,<br>
s mert csak Veled szép e földi élet:<br>
remélem, Isten sokáig éltet &ndash;<br>
a magas Égnek köszönlek Téged...
</p>

