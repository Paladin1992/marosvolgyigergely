<h3>Indulás előtt</h3>

<p>
Utolsó napom ez itthon:<br>
s vágyaim majd útra kelnek.<br>
Hátrahagyom minden titkom,<br>
míg a percek gyorsan telnek.
</p>

<p>
Megtelik a bőrönd, táska<br>
(szívemet is bepakolom),<br>
s aki szembejön, majd látja:<br>
kis helyen elfér otthonom.
</p>

<p>
Amikor a célba érek,<br>
előszedek mindent, mi kell;<br>
kibújik majd álom, lélek,<br>
s virágom tán ott is kikel...
</p>

<p>
Ha érzéseim elhozom,<br>
honvágyam lesz minden színben,<br>
hisz más tájékra tartozom,<br>
és hazahúz, míg van szívem!
</p>

