<h3>Mikor szemedbe nézek...<br>
<span class="subtitle">(T. B. részére)</span></h3>

<p>
Mikor szemedbe nézek,<br>
merengő tengert látok:<br>
hol fénytől ittas vágyakat<br>
ringatnak szelíd hullámok...
</p>

<p>
Mikor szemedbe nézek,<br>
csillagos eget látok:<br>
de néha zord felhőket is,<br>
hol csapkodnak a villámok...
</p>

<p>
Mikor szemedbe nézek,<br>
zöldellő tájat látok:<br>
hol a langyos szél szavára<br>
kinyílnak tarka virágok...
</p>

<p>
Mikor szemedbe nézek,<br>
örök változást látok:<br>
hol tavaszi rügyek alatt<br>
az őszi avarban járok...
</p>

<p>
Mikor szemedbe nézek,<br>
egy nyugodt lelket látok:<br>
s mélyen benne apró szikrát,<br>
mi szívekben gyújthat lángot...
</p>

<p>
Mikor szemedbe nézek,<br>
nehéz könnyeket látok:<br>
miket fájón elfojtanak<br>
az akarat szőtte láncok...
</p>

<p>
Mikor szemedbe nézek,<br>
reményt csak arra látok:<br>
hogy ezentúl már többek is<br>
lehetnénk, nem csak barátok...
</p>

<p class="writing-info">
Nagymaros,<br>
2012. január 13-15.
</p>