<h3>Nincs ihlet</h3>

<p>
Nos, igen: van olyan, hogy nincs ihlet,<br>
s ezt egy költőnek nehéz kibírni:<br>
mereven ülni a homályban egy<br>
lapra meredve, mert nincs mit írni!
</p>

<p>
Nem szárnyal az a tarka képzelet,<br>
a kínoktól nem lehet, csak sírni;<br>
s furcsa, de talán e kis vershez is<br>
ihlet kellett, hogy le tudjam írni...
</p>

