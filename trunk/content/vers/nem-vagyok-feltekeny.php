<h1>Nem vagyok féltékeny...</h1>

<p>
Ne félj, nem vagyok féltékeny,<br>
rosszakaró meg végképp nem...<br>
Csak a szemét vájnám ki,<br>
mivel rád nézett...<br>
Csak a torkát vágnám el,<br>
mivel lélegzett...<br>
Csak az ujját törném el,<br>
mivel hozzád ért...<br>
Csak a szívét tépném ki<br>
a szerelmedért!...
</p>

