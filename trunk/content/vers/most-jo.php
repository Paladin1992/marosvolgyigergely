<h3>Most jó</h3>

<p>
Most jó lesz, mert egész,<br>
most könnyű, mert nehéz;<br>
most lehull, mert virít,<br>
most hervad, mert kinyílt;<br>
most harcol, mert békít,<br>
most rombol, mert épít;<br>
most lángol, mert heves,<br>
most talál, mert keres;<br>
most csendes, mert hangos,<br>
most forró, mert langyos;<br>
most langyos, mert hideg,<br>
most selymes, mert rideg;<br>
most vidám, mert dalol,<br>
most őriz, mert karol;<br>
most hamis, mert igaz,<br>
most bátor, mert pimasz;<br>
most érett, mert zsenge,<br>
most erős, mert gyenge;<br>
most kicsik, mert nagyok,<br>
most sötét, mert ragyog;<br>
most reszket, mert ölel,<br>
most távol, mert közel;<br>
most kivár, mert marad,<br>
most repül, mert szabad;<br>
most bájos, mert kerek,<br>
most felnőtt, mert gyerek;<br>
most muszáj, mert lehet,<br>
most boldog, mert nevet:<br>
most igen, mert szeret!<br>
</p>

<p class="writing-info">
Nagymaros,<br>
2014. július 23-24.
</p>