<h3>Őrizd meg a pillanatot</h3>

<p>
Pereg az idő,<br>
a szürke hétköznapok,<br>
s tán nem veszed észre<br>
mindazt, mit a sors adott:<br>
a tengernyi csodát,<br>
mi léted szövi át,<br>
s a millió varázslatot,<br>
mi megadatott,<br>
hogy kifesse színeivel<br>
minden egyes napod...<br>
Ne hagyd őket elenyészni &ndash;<br>
őrizd meg a pillanatot!
</p>

<p>
Őrizd meg,<br>
mikor először köszönnek<br>
rád a bimbózó rügyek,<br>
s te &ndash; ügyet<br>
sem vetve a város zajára &ndash;,<br>
megállsz az út mentén<br>
egy orgona illatára,<br>
s míg elmerülsz így a kéjben,<br>
sejteni kezded,<br>
milyen lehet az Éden.
</p>

<p>
Őrizd meg,<br>
mikor rád talál egy kedves<br>
kis vers<br>
vagy egy régi dallam,<br>
s te azt dúdolod halkan,<br>
magányos estéiden át,<br>
mikor szikrázó csillagok<br>
néznek le rád<br>
a mérhetetlen égből,<br>
és könnyed hull a felismeréstől,<br>
hogy bármekkora is az árnyék,<br>
mi rád vetül,<br>
a legnagyobb ajándék:<br>
hogy sosem vagy egyedül.
</p>

<p>
Vagy mikor úgy érzed:<br>
kudarc minden döntésed,<br>
s csak kínjaid halmozod,<br>
és nem tudod a jó irányt:<br>
őrizd meg a pillanatot,<br>
mikor hirtelen kiránt<br>
a bajból egy régi barát,<br>
egy tettre kész,<br>
segítő kéz,<br>
ki jobbját<br>
nyújtja a szakadékon át,<br>
s kinek hálával mondhatod:<br>
„Tudtam, hogy rád számíthatok!”
</p>

<p>
Őrizd meg a pillanatot,<br>
mikor megkapod, mire vágysz;<br>
vagy mikor elmúlik a gyász,<br>
és a bánat ködén<br>
áthatol a fény,<br>
mint mikor egy őszi napon<br>
megcsillan a Balaton<br>
vizének sima tükre,<br>
s az avarba feküdve<br>
&ndash; nem törődve koroddal &ndash;<br>
megtelik szíved ezer mosollyal.
</p>

<p>
Őrizd meg a pillanatot,<br>
mikor meglátod őt<br>
a napsütötte parton,<br>
ahogy ott ül egy padon,<br>
hívogató lombok alatt,<br>
és végül csak a csend,<br>
a suttogó csend marad,<br>
s egy érzéki pillanat,<br>
mit felperzsel a mámor,<br>
mielőtt ajkad ajkára ráforr...<br>
És ahogy megfogja kezed,<br>
már érzed,<br>
hogy először találkoztál<br>
azzal, kire vágytál:<br>
az igaz szerelemmel...<br>
És már tudod: ő az az ember,<br>
ki majd vágyakozó szemmel<br>
néz rád minden reggel,<br>
kinek karjaiban enyhül a nyár,<br>
és forró a december.
</p>

<p>
Őrizd meg a percet,<br>
mikor hazatérsz,<br>
és belépsz<br>
azon a küszöbön,<br>
mely bevezet a házba,<br>
mely legkedvesebb<br>
neked<br>
e Földön,<br>
hogy aztán nyugodt szívvel<br>
merengj el egy ponton:<br>
milyen jó, ha van haza,<br>
és van egy otthon!
</p>

<p>
Őrizd meg a pillanatot,<br>
mikor kőszívűnek hitt apád<br>
elérzékenyülve néz rád,<br>
és elcsukló hangon<br>
azt mondja halkan:<br>
„Örülök, hogy ilyen fiam van...”<br>
Vagy mikor annyi dacos éven át<br>
még mindig önzetlen<br>
szeret és megbocsát<br>
édesanyád,<br>
kit hidegen hagy,<br>
hány éves vagy,<br>
mert felnőttként is óva int,<br>
hogy „Öltözz melegen!”,<br>
mikor elindulsz<br>
egy hűvös reggelen,<br>
s még utadon<br>
is érzed magadon<br>
féltő tekintetét,<br>
mintha karjait fonná köréd...
</p>

<p>
Őrizd meg<br>
szívedben őket,<br>
mikor már nem élnek,<br>
és a percet,<br>
mikor először szegezed<br>
tekinteted<br>
hálával az égnek,<br>
mert megérted,<br>
mit áldoztak föl<br>
szeretetből<br>
teérted,<br>
kik büszkén néznek rád<br>
(immár vonásaidon át)<br>
a tükörből.
</p>

<p>
Őrizd meg a pillanatot,<br>
mikor először tarthatod<br>
karjaidban gyermeked,<br>
e testet öltött, apró életet,<br>
ki majd kíváncsi szemmel méreget<br>
mindent, mit te már rég elfeledtél,<br>
mire „mindentudó” felnőtt lettél &ndash;<br>
s így tör rád a nagy felismerés,<br>
hogy naiv tudásod mily kevés,<br>
s hogy mennyi igaz játék<br>
és mennyi talány<br>
vár még<br>
mesekönyvek színes oldalán,<br>
s talán<br>
újra el mered hinni,<br>
ahogy eléd tárul egy családi kép,<br>
hogy az élet mégis csak csodaszép!
</p>

<p>
Őrizz meg magadban<br>
mindent, mit lehet,<br>
de semmit, ami fáj &ndash;<br>
őrizz meg mindent, amit tanultál:<br>
de ne a lexikonok mélyét,<br>
hanem az élet teljességét:<br>
az örömöket,<br>
szerelmeket,<br>
s a legszebb égi áldást:<br>
az ölelést és megbocsátást.
</p>

<p>
Úgy őrizz minden pillanatot,<br>
miként hajnal fűzi<br>
gyöngyökként a harmatot,<br>
és úgy tekintsed,<br>
hogy csak ez lehet kincsed,<br>
mit elvihetsz magaddal,<br>
mikor lelkedet simogatva<br>
szól neked egy angyal,<br>
mosolygó szemekkel:<br>
„Jöjj, gyermekem &ndash; indulni kell...”
</p>

