<h1>A Szeretet ünnepe</h1>

<p>
Ma beteg világban élünk,<br>
Céltalanul mendegélünk.<br>
A becsület itt már nem él,<br>
Csak a pénz, mi gyarlón beszél.<br>
Erkölcsünk is kihalóban,<br>
Már igazság sincs a borban.<br>
Áldozatot nem kell hozni:<br>
Hisz egyszerűbb siránkozni!
</p>

<p>
A termőföld már sivár lett,<br>
Az utolsó tűz elégett...<br>
Lehulltak mind a csillagok,<br>
Az árnyékok most túl nagyok...<br>
Elsötétült a holdvilág,<br>
Hervadtan áll minden virág:<br>
Szívverésünk végleg lanyhul,<br>
S fölöttünk már bealkonyul...
</p>

<p>
Ha lesz gyógyír a sebekre,<br>
S jut család minden gyerekre...<br>
Ha majd Isten miránk nevet,<br>
S elönt a lelkiismeret...<br>
Ha többé nem fegyverkezünk:<br>
De mind jó testvérek leszünk,<br>
S ha szívünkben béke honol:<br>
Többé nem kell Menny, se Pokol...
</p>

<p>
Ha fent már csak az ég ragyog,<br>
S velünk élnek az angyalok;<br>
Az ember nem öl, nem kapzsi,<br>
S rájön: jobb adni, mint kapni!...<br>
Akkor élünk majd szabadon:<br>
Osztozunk, mert nincs tulajdon;<br>
S zengjük, egymást átölelve:<br>
„Itt a Szeretet ünnepe!”
</p>

