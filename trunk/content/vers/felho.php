<p>
    <i>Eredeti szöveg:</i>
<p>

<h3>József Attila &ndash; Wolke</h3>

<p>
Du wirst nicht meine Frau sein,<br>
du wirst leicht sein wie eine Wolke.<br>
Ich schliesse mich ins Feld herein,<br>
dass ich dich da erwarten sollte.
</p>

<p>
Du wirst herkommen in mein Haus,<br>
in meine sehr kühles ohne Dach dann:<br>
Ich werde traurig, wie die Maus,<br>
auf die es regnet, leise, langsam.
</p>

<p><i>Fordítások:</i></p>

<h3>József Attila &ndash; Felhő<br>
<span class="subtitle">I. változat (szabadabb)</span></h3>

<p>
Tán nem leszel a kedvesem,<br>
de mint felhő lebegsz, könnyedén.<br>
Csekély magam földbe vetem,<br>
s eztán ott fogok rád várni én.
</p>

<p>
Ha házamba térsz vendégségbe,<br>
tető nélkül vacogok majd halkan,<br>
és bús leszek, mint kis egérke,<br>
mikor eső hull rá némán, lassan.
</p>

<p class="writing-info">
Nagymaros,<br>
2015. december 22.
</p>

<h3>József Attila &ndash; Felhő<br>
<span class="subtitle">II. változat (szöveghűbb)</span></h3>

<p>
Te nem leszel a kedvesem,<br>
te lágy leszel, mint zsenge felhő.<br>
Csekély magam földbe vetem,<br>
hogy várjalak, míg időd eljő.
</p>

<p>
Majd házamba térsz vendégségbe,<br>
mely tetőtlen, fagyos, lakhatatlan:<br>
s én bús leszek, mint kis egérke,<br>
mikor eső hull rá némán, lassan.
</p>

<p class="writing-info">
Nagymaros,<br>
2015. december 23-24.
