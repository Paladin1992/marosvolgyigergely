A halálért élünk e földi életben?<br>
Jó csak azt éri, ki képes várni arra,<br>
Bűnös, aljas, gonosz gyilkos: ez az ember!<br>
S ki belül becsületes, lelke is tiszta,<br>
Ártatlan bár: a sötétség pusztítja el!
</p>

<p>
Bűnsivatag ez, hol apad az igazság:<br>
S én az Úrhoz imádkozom szenteltvízért,<br>
