Hisz a dicsőségnek lángja nem oltalom,<br>
S végül biztos szívébe szúr egy véres kard!<br>
A vesztes marad alul a jobbal szemben,<br>
Eget rengető hullámokkal is küzdhet,<br>
Én félve mondom: igaz hitre neveltek,<br>
Hát erős leszek, s nem hullajtok könnyeket!
</p>

<p>
