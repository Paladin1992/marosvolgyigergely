<p>
A hajnali dér szálla már alá lassan,<br>
S a messzi alvó hegyek színe mind zord kék,<br>
De még a hold fénylik az égen magasan,<br>
Suhanó szellő fülembe sugdolózék:<br>
Hisz őt évek óta barátként kedvelem,<br>
Ki által álmomat éri az enyészet;<br>
Szavára én a hideg földről felkelek,<br>
S engem lágy mosollyal köszönt a természet.
</p>

<p>
Szép álmom hű páncélos lovagja meghalt...<br>
De ennek vége már; el kell, hogy feledjem!<br>
Testem poros, rongyos a ruhám és szakadt,<br>
Hosszú az út, mit ez idáig megtettem.<br>
Én biz’ nem tudhatom, merre visz majd Sorsom,<br>
Tán Isten a tudója, de Ő elhagyott,<br>
Nincs nekem boldogító, meleg otthonom,<br>
Szívem nem érez többé, hisz rég megfagyott.
</p>

<p>
