És lelkem végleg feledje a bűn ízét!<br>
Lassan olybá tűnik, beláthatom végre,<br>
Tán örökre szertefoszlott minden álmom,<br>
&ndash; Remény nélkül tekintek fel a zord égre &ndash;<br>
S csak semmivé lett jövőm hamvait látom.
</p>

<p>
„Isten gyermekei vagyunk” &ndash; mind ezt mondják,<br>
S Ő visszatér majd, csak várjunk rá eleget...!<br>
De biz’ nem jöve, hisz rég elhagyott "Atyánk":<br>
Országa már eltűnt, s átkoztam az eget.<br>
A megváltás nem ragyog angyali fénnyel;<br>
Csak annyi, mint gyertya az éj sötétjében,<br>
Apró lángja néki megszökhet a széllel...<br>
A hívő embert hagyván örök kétségben.
</p>

<p>
Miért van az úgy &ndash; a választ nem találom &ndash;,<br>
