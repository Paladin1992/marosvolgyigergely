<h1>Emlékszel?</h1>

<p>
Emlékszel még, kedvesem?<br>
Kint jártunk a szigeten:<br>
szép nyári éjszaka volt,<br>
fölöttünk a telihold,<br>
s mi a vízparton ülve,<br>
mély ábrándba merülve<br>
ölelkeztünk gyöngéden<br>
(egy csillag hullt az égen);<br>
csak ezüstös fény látszott,<br>
az idő semmit játszott,<br>
lágy sóhaján a szélnek<br>
szállt sok mosolygó lélek,<br>
az ágon ülő csendnek<br>
kis hullámok feleltek:<br>
s lanka vízen a vágyak<br>
(mint kéj-ringató ágyak)<br>
dús csillagporban lengtek,<br>
s némán úsztak a percek,<br>
míg halk tücsökzenében<br>
merültünk el az éjben...<br>
Majd erőm összeszedtem,<br>
s ifjú lángokkal telten<br>
puha kezedhez értem,<br>
és még többet remélten<br>
egyre közelebb bújtam,<br>
hogy halkan odasúgjam<br>
&ndash; míg szemed tüze égett &ndash;:<br>
nagyon szeretlek téged!
</p>

