<h3>Nagymaros</h3>

<p>
Hol a vén Duna völgybe ér,<br>
s díszíti sok zöld hegyorom,<br>
ott simul hozzá Nagymaros,<br>
egy kis város &ndash; az otthonom.
</p>

<p>
Visegrádnak büszke gyöngye<br>
hegytetőről néző vára,<br>
s drágakőként vele ragyog<br>
a királyi panoráma.
</p>

<p>
A gesztenyés némaságát<br>
kifestik a madárhangok,<br>
míg bezengik e tájékot<br>
a buzgón lengő harangok.
</p>

<p>
Duna-parton áll a Dám: a<br>
várost jégzajláskor óvta;<br>
s Pestig nyúl a sziget, mi a<br>
folyót két testvérre osztja.
</p>

<p>
Van olyan, ki hajóra száll,<br>
s van, ki netán kompért fizet;<br>
ám akad, ki szép időben<br>
kajakkal szeli a vizet.
</p>

<p>
Csodás, ahogy a vár mögött<br>
nyújtózva kel a nap reggel;<br>
vagy amint nyugovóra tér<br>
búcsúzó madársereggel.
</p>

<p>
Mikor zöld sátrait veri<br>
a varázslatos kikelet,<br>
új rózsaszínben ragyognak<br>
a Fő téri falevelek.
</p>

<p>
Nyáron sokan járnak erre,<br>
hisz e táj akár az Éden;<br>
vidám emberek fürdőznek<br>
s pihennek a tüzes fényben.
</p>

<p>
Aranyat és rőt-bíbort szór<br>
az ősz a Dunakanyarra,<br>
míg a sétányt a zizzenő<br>
avar némán betakarja.
</p>

<p>
A víz sima tükrét télen<br>
komor jégtáblák hasítják,<br>
s a hóval fedett utcákon<br>
szikrát vetnek az éjszakák.
</p>

<p>
Bárhova is visz a sors &ndash; ha<br>
idegen szél is karol át:<br>
ígérem neked, Nagymaros &ndash;<br>
míg élek, leszek hű hozzád!
</p>

<p>
Hisz nekem csak ez a hely, hol<br>
szívemben is otthon vagyok,<br>
s tán itt szórják el poraim,<br>
ha hívnak majd... a csillagok.
</p>

<p class="writing-info">
Nagymaros,<br>
2012. február 25-27.,<br>
2012. március 1-7.
</p>