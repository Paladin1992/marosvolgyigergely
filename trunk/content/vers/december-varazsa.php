<h3>December varázsa</h3>

<p>
Cipőmmel a hóban ropogva járván,<br>
Ama szürke eget felettem látván,<br>
A hóval lepett kopasz tölgyfák alatt,<br>
Pelyhecskék odafentről szállnak alant;<br>
Cinkéket megpillantom: elreppennek,<br>
Elhallgattatván a lágy természetet:<br>
Szeretettel melengetve szívemet,<br>
S én mind erősebbnek érzem hitemet.
</p>

<p>
Csak magam járom a sétányt egyedül,<br>
Lelkem így tán a gondoktól menekül:<br>
Csak gondolatok nélkül elmélkedem,<br>
A hóeséstől senki sem lát engem;<br>
Így, december közepe felé járván,<br>
A szent karácsony közeledtét várván,<br>
Sétálván a homályos utcafényben,<br>
Hópelyhek, mint csillagpor hullnak szépen.
</p>

<p>
A kirakatok ragyogásban úsznak &ndash;<br>
Már behódoltam a fenyőillatnak &ndash;,<br>
Fehér karácsony a legszebb kívánság,<br>
Gyújtsd meg a negyedik adventi gyertyát;<br>
Asztalhoz ülvén a család körében,<br>
Meleg étel, a szeretet ölében,<br>
E csodás érzés szívem fojtogatja,<br>
S várva-várom: legyen karácsony újra!
</p>

