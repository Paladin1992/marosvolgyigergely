Te vagy a rózsa kertemben,<br>
a szivárvány kék egemen,<br>
napnak fénye borús égen,<br>
gyertya, mi fénylik az éjben...
</p>

<p>
Te vagy minden lélegzetem,<br>
a megtestesült szerelem &ndash;<br>
kulcsa szívemnek csak te vagy:<br>
nyisd hát ki, és el sose hagyj!
</p>

<p>
Te vagy elmém látomása,<br>
bűneim feloldozása,<br>
nagy bánatom pusztítója,<br>
s vágyaimnak csitítója...
</p>

<p>
Eső vagy a sivatagban,<br>
menedék a zivatarban,<br>
kandalló a hideg házban,<br>
kedvesem a meleg ágyban...
</p>

<p>
Lángoló tűz a pokolban,<br>
színes virág úti porban,<br>
mennyekben csillag, mely ragyog:<br>
a nő, kiért majd’ meghalok...
</p>

<p>
Míg szívedből tisztán szeretsz,<br>
tőlem bizony bármi lehetsz;<br>
csak arra kérlek: légy velem,<br>
mert Te vagy az, ki kell nekem...
</p>

