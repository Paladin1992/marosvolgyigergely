<h1>Csak mi ketten<br>
<span class="subtitle">(T. N. részére)</span></h1>

<p>
Júniusi este volt, ültünk a Balaton partján. A pici hullámoknak halk csobogással feleltek a sziklák; a kikötőben szenderegve ringatóztak a vitorlások. Idehallatszott a nádas felől a békák brekegése. A túlpartot csak egy halovány fénysor körvonalazta; a hegyek beleolvadtak a csillagos, fátyolfelhős égboltba. A Hold akkor éppen más féltekén lévő párokat boldogított, de mi mégis örültünk, mert így legalább hagyta kedvenc csillagainkat tündökölni.
</p>

<p>
Szélcsend volt. A betonszegély még őrzött valamennyit a délutáni napsugarak melegéből; éppen annyit, hogy kellemes legyen ülni rajta... egymás mellett... karnyújtásnyira...
</p>

<p>
Végre együtt lehetünk &ndash; megint!
</p>

<p>
Odahajoltam hozzád, fejemet mellkasodra téve, és csak hallgattam a lüktetést... Felváltva dobbant a szívünk: dobbant a tiéd, dobbant az enyém &ndash; mintha egymásnak felelgettek volna. Egyszerre megszűnt bennem minden büszkeség, amiről azt hittem, hogy a férfi létet jelenti... Gyermek módjára kapaszkodtam beléd, mintha te maradtál volna az egyetlen menedékem, nehogy a világ megtaláljon! A világ, mely folyton csak akadályokat gördít közénk, újabb és újabb kihívásokat, elvárásokat aggat ránk, és teljesen kimerít lelkileg...!
</p>

<p>
Pihenni vágytam. Elnyúltam a betonon, fejemet öledbe hajtottam, és kémleltem a csillagokat... Tenyeredet simogatva húztad végig arcomon, egészen a homlokomig, lassan, oda-vissza. Lehunytam a szemem... Egyszerre minden belső feszültség és dübörgés lanyhulni kezdett bennem, mígnem kitisztult az elmém. Egy kellemes érzés járta át a testem, és úgy éreztem, mintha minden megszűnt volna körülöttem... mintha két világ között lebegnék...
</p>

<p>
Mikor kinyitottam a szemem, épp egy hullócsillag suhant el felettünk. Azt mondtad, gyorsan kívánjak valamit &ndash; mégse tettem. ...Mert nem volt rá szükség. Mert ott, akkor, abban a pillanatban &ndash; Veled &ndash; mindenem megvolt. És ennél többet nem tudtam volna kívánni...
</p>

