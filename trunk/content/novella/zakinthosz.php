<p>
Állítólag egyszer József Attila azon vitatkozott egy barátjával, hogy milyen színű a tenger. Az illető szerint a tenger színe kék, míg költőnk zöldnek vélte. Végül Attila így zárta le a vitát: „A tenger színe valóban kék, de nem úgy, ahogy te gondolod.”
</p>

<p>
És mennyire igaza volt! Mert azt a színt nem lehet elképzelni, nincs az a fénykép, mely teljes valójában visszaadná. Mert az a kék &ndash; mely az ékes türkiztől kezdve, a tajtékzó mélykéken át, egészen a viharos szürkéig terjed, és mely egyszerre nyugalom, zabolátlan vadság, szirtfal-omlasztó erő, határtalan szabadság &ndash; maga a végtelen. Olyan ősi elem, melybe az ember vagy csak úgy hirtelen beleveti magát, vagy hagyja, hogy lépésenként körülölelje. Egy hullámzó csoda, melyben akár csak tekintettel, akár egész testtel jó elmerülni...
</p>

<p>
Márpedig ez volt az a kékség, mely zakinthoszi szobám erkélyéről köszöntött minden reggel, mikor a horizont még lágy aranyban és rózsaszínben úszott... Mondd, érezted már bőrödön azt a nyárvégi, tengeri levegőt hajnalban, amikor életörömmel telve ébredsz minden egyes nap?
</p>

<p>
Próbáltál már valaha egy szirtfal oldalán 6 méter magasan lévő keskeny pallóról a kék mélység fölé állni? Tudod, milyen tudatosan legyőzni a félelmedet, hogy le merj ugrani? És érezted már utána azt a felszabadultságot, amikor nem marad benned a „mi lett volna, ha...” nyugtalanító gondolata?
</p>

<p>
Utaztál már egy jó humorú, görög buszsofőrrel, aki elé a túravezető odatartja a mikrofont, hogy az éppen rádióból szóló klasszikus görög dalból pár sort énekeljen, miközben a sziget hegyvidékének szerpentines útjain kanyarogva rálátni az éjszakai kisvárosok fényeire?
</p>

<p>
Vagy béreltél-e már valaha autót egymagad, hogy megízleld a szabadság bódító menetszelét, amikor minden kereszteződésnél te döntesz, hogy merre mész?  Amikor van nálad nyomtatott térkép és GPS, mégis eltévedsz egy teljesen ismeretlen helyen, de te csak mosolyogsz az egészen? Ugye tudod, hogy ezt hívják kalandnak?
</p>

<p>
És azt tudod, milyen Zante városkájának szűk utcáin barangolva betévedni egy olasz fagylaltos bácsihoz, aki már évtizedek óta feleségével együtt készíti azt az igazi, krémes fagylaltot? És ha látnád szemében a hálát, mikor az anyanyelvén, kezdetleges olaszoddal próbálsz kérni pár gombócot, hogy néhány kedves szó után egy baráti <i>Arrivederci!</i>-vel köszönjetek el egymástól!
</p>

<p>
És ha már Görögország, kóstoltad a görögsalátát, az ottani olívaolajat? Vagy azt az isteni toast szendvicset pirított baconnel, paradicsommal, salátával, tojással és sajttal nyakon öntve? És a fahéjas muszakát? És a tésztás tenger gyümölcsei tálat rákkal és kagylókkal? Vagy ettél már olyan gyümölcssalátát, melyben a sárgadinnye, a barack, a banán, az eper, a dió, egy adag joghurt és egy folyásnyi méz harmóniája kényeztet?
</p>

<p>
Láttad már valaha nyílt vízen a naplementét egy szirtfal tetejéről, amikor úgy tűnik, mintha az az izzó korong vörösen beleolvadna a tengerbe? És utána azt a gyönyörű alkonypírt? És ismered az érzést, mikor nem akarsz még hazamenni?...
</p>

