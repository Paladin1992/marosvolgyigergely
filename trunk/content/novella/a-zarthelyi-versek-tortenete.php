Na, ezt muszáj elmesélnem!
</p>

<p>
Én a Pannon Egyetem programtervező informatikus hallgatója voltam Veszprémben 2012-től 2016-ig. Bár sokan furcsán szoktak rám nézni, hogy mit keres egy versszerető ember az informatikusok között (vagy mit csinál egy informatikus a versszeretők között), én úgy gondolom, hogy nem baj &ndash; sőt, igenis fontos! &ndash;, ha az ember szélesebb érdeklődési körrel rendelkezik, mint amilyeneket a társadalmi elvárások diktálnak.
</p>

<p>
Szakomon több mint negyven tantárgyat kellett teljesíteni, és néhány kivételtől eltekintve, egyik sem okozott komolyabb gondot &ndash; amelyik viszont igen, az nagyon. Persze utólag minden olyan könnyűnek tűnik, mert végül rájön az ember, hogy nem feltétlenül az akadállyal van probléma, hanem a hozzáállással...
</p>

<p>
Szóval az egyik ilyen tárgy, amivel így küzdöttem, ezt a nevet viselte: Számítógép-architektúrák. (Karunk berkeiben viszont mi ezt egyszerűen csak „szar”-nak rövidítettük. Egy külsős ember számára furcsán tud hatni egy ilyen mondat: „Szarod meglett?” vagy „Éppen a szarral küzdök!”, de e szóviccen kívül más örömöt nem nagyon lehetett benne lelni...) Ezt a tárgyat mintatanterv szerint a második félévben kellett volna teljesíteni, és mivel csak tavaszi félévben van, így bukás esetén várni kell egy teljes évet, mire újra fel lehetne venni. Nos, én is így jártam el 2014 tavaszán &ndash; immár másodjára élvezvén e „csodás” tananyagot.
</p>

<p>
A tárgy követelményei szerint egy zárthelyi dolgozatot kellett írnia mindenkinek, amit ha legalább 4-esre megír valaki, az megajánlott jegyet eredményez, magyarán nem kell vizsgázni. Ha viszont a 2-es szintet még a pótZH-n sem éri el a hallgató, az egyenlő a bukással.
</p>

<p>
2014. május 6-án tehát eljött az igazság pillanata. Valamennyit készültem, de csak imádkoztam, hogy a tudásnak aligha nevezhető aprócska halmaz a fejemben elég legyen egy 2-es összekaparásához. (Az ima azért jó, mert megy fejből &ndash; nem úgy, mint a papíron lévő feladatok!) A számolós példákat ugyan megcsináltam, de ha mind hibátlan, akkor sincs meg az elégséges szint. Esélytelen helyzetemet nagyjából 20 perc alatt felmértem, így a többiek még javában írták a dolgozatot. A tanár kérte, hogy ne másszunk át társainkon, ha készen vagyunk, hanem várjunk a helyünkön, míg ki nem tudunk jönni a padsorból. Rendben. De kezdtem unatkozni. Mit csináljak? Mit csináljak?... Kinéztem az ablakon, láttam, hogy gyönyörűen virágoznak a napsütötte fák, és lágyan fújja a zöldellő lombokat a szél. Bárcsak kint lehetnék... Megvan! Írok erről egy verset! ...De hova? Áh, leírom ide a dolgozatra.
</p>

<p>
Így tehát elkezdtem írni egy versikét a rosszul lerajzolt RLC áramkörös ábrám alá (ne kérdezd, hogy mi az, azóta se tudom!). Végül ez lett belőle:
</p>

<i>
    <p>
    Számítógép-architektúrák<br>
    bizony nem a kedvenc tárgyam,<br>
    csak kint lenni a friss levegőn:<br>
    ez lenne most minden vágyam!
    </p>

    </p>
    Oh, de jaj, azt sajnos nem lehet,<br>
    mert itt kell nekünk bent ülni;<br>
    s folyton csak az jár a fejemben,<br>
    hogy kellett volna készülni!
    </p>
</i>

<p>
Kíváncsi voltam, a tanár hogyan fogja díjazni mindezt &ndash; hátha kapok rá némi plusz pontot. Sajnos nem. Dolgozatom olyan jól sikerült, hogy egy héttel később jöhettem megint.
</p>

<p>
2014. május 13-án tehát ismét ott ültem az A2-es teremben, hogy bizonyítsak. A lapok kiosztása és a tudnivalók ismertetése után a tanár még hozzátette: „Ja, a múltkor az egyik kollegától kaptam egy nagyon kedves kis verset. Én biztatok mindenkit, tényleg. Szóval verset, mesét, balladát is nyugodtan lehet írni &ndash; csak a tananyagról szóljon!”
</p>

<p>
Ekkor már felkészültem annyira, hogy a múlt heti feladatsort legalább 3-asra meg tudtam volna írni, de arra nem voltam felkészülve, hogy a tanár a pótZH-ba mindegyik feladattípusból a legnehezebb fajtát teszi be, úgyhogy még annyi esélyem se volt, mint múltkor. Sebaj &ndash; tanulván hibámból, most a padsor szélére ültem, de úgy voltam vele, hogy ha már a tanár is biztat versírásra, akkor illene valamit alkotni a papírra! Így született meg ez:
</p>

<i>
    <p>
    Készülni bár készültem,<br>
    de nem ezeket vártam,<br>
    s van itt egy-két feladat,<br>
    amit még sose láttam!
    </p>

    <p>
    Mérnök ugyan nem leszek,<br>
    sem a nemzet költője,<br>
    de mi most nem sikerült,<br>
    sikerül majd jövőre!
    </p>
</i>

<p>
A „Mérnök ugyan nem leszek” sor arra utal, hogy a tanárunk sokszor mondott ilyeneket: „mivel Ti mérnökök lesztek, ezért...” vagy „egy mérnöknek illik tudni, hogy...” &ndash; ezzel viszont én nem teljesen értettem egyet.
</p>

<p>
Nos, a versírás befejeztével lebattyogtam a lépcsőn, hogy beadjam a dolgozatom. Ahogy nyújtottam át a tanárnak a papírt, rezzenéstelen arccal így szóltam: „Írtam verset... megint.........Majd írok jövőre is.” A tanár elmosolyodott, aztán ahogy indultam visszafelé a lépcsőn, szelíden utánam szólt: „Örülnék, ha jövőre sikerülne a tananyagot is elsajátítani!” Bólogatva visszamosolyogtam rá.
</p>

<p>
Egy évvel később már rendesen felkészültem, és ebből kifolyólag nem jutott idő a versírásra, mert örültem, hogy egyáltalán a feladatokat be tudtam fejezni. Mikor adtam be a dolgozatot, magabiztosan mondtam a tanáromnak:<br>
&ndash; Ha ez nem lesz meg...!<br>
&ndash; Akkor...? &ndash; kérdezte szemöldökét felhúzva.<br>
&ndash; Akkor írok egy verset...
</p>

<p>
Szerencsére meglett. Megajánlott 4-est kaptam.
</p>

