<p>
Nem kívánom, hogy enyém légy &ndash; elég, ha hozzám tartozol; mert nem tárgy vagy, amit birtokolni lehet, hanem teljes értékű Ember, akihez kellemes kötelék fűz. De kérem, hogy add magad &ndash; minden pillanatban; s hogy együtt mártózzunk a hűség tengerében.
</p>

<p>
Tárd ki szíved kapuit, engedj be magadhoz, s én elszórom kertedben a bizalom édes gyümölcsfáinak magjait. Milyen jó is lesz majd abból falatozni nehezebb napokon, a biztonság árnyas lugasainak lombja alatt!...
</p>

<p>
Erősen vagy lágyan, de fogd a kezem, és ne engedd el... Bújj hozzám, ha fázni kényszerít e rideg világ; ha menedékre van szükséged; ha közelségem megnyugtat; vagy csak hajtsd tarka álomra fejed ölemben, ha elfáradtál &ndash; s majd én is úgy teszek. Ne szégyenkezz előttem: nekem így vagy tökéletes &ndash; hát vetkőzd le meztelenséged, hadd lássam, mily kincsek rejtőznek a felszín alatt, és engedd őket ragyogni.
</p>

<p>
Nem kérem, hogy kimondd: szeretlek &ndash; de az egész lényed sugározza, hogy így van. Ha néha mégis fülembe súgod, s én cserébe csak szótlanul, csillogó szemekkel elmosolyodom, vagy telhetetlenül átölellek, ne vedd zokon: akkor épp így szeretném kifejezni, hogy én is ugyanezt érzem irántad! Mert nem szóval szeret az ember, hanem szívvel, lélekkel és legfőképp: a tetteivel...
</p>

<p>
Ha mélyen szemedbe nézek, de ajkam mozdulatlan, akkor épp lelked gyöngyöket rejtő tengerének titkos nyelvén szólok hozzád. Ha ölellek, nem csupán azért teszem, mert erre kér tekinteted, hanem mert sürgetőn éget a vágy, hogy karjaimba zárva érezzelek magam körül. Nem tehetek róla: annyira szeretem hallgatni, ahogy közelségünktől hevesebben ver a szívünk...
</p>

<p>
Amikor veled vagyok, lágy melegség tölt el, s mindig felperzsel az érzés, ha ujjaimmal simogatva barangolhatok testeden; ahogy sóhajaink meg-megtörik a csendet, s ahogy izmaink megfeszülnek, majd elernyednek megint, akár partra nyúló, szelíd hullámok... Néha irigység kerülget, mert te vagy rá az élő bizonyíték, hogy a természet milyen páratlan gyönyörűség teremtésére képes!...
</p>

<p>
Ne haragudj rám, ha olykor nehezemre esik elengedni téged &ndash; féltelek; ilyenkor a bennem lévő gyermeki lélek zokog, mert fél... fél, hogy elveszíti másik felét. Mert az vagy: lelki társam. Külön-külön csak árnyai vagyunk önmagunknak, de együtt: az egésznél is többek!
</p>

