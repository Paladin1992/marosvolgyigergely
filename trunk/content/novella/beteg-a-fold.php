<div class="subtitle">A Föld napjára</div>

<p>
A Föld elmegy az orvoshoz:<br>
&ndash; Jó napot, doktor úr!<br>
&ndash; Jó napot, foglaljon helyet. Volt már itt korábban?<br>
&ndash; Még nem.<br>
&ndash; Akkor először felvesszük az adatait. Neve?<br>
&ndash; Föld.<br>
&ndash; Született?<br>
&ndash; Körülbelül 4 és fél milliárd éve.<br>
&ndash; Lakcíme?<br>
&ndash; Univerzum, Tejút, Naprendszer, Naptól számítva a harmadik bolygó.<br>
&ndash; Magassága?<br>
&ndash; Nos, alkatilag olyan típus vagyok, akire viccelődve azt szokták mondani, hogy szélte, hossza egy. Úgyhogy magasság helyett én inkább átmérőt mondanék, ami emlékeim szerint 12 ezer 756 kilométer.<br>
&ndash; Értem. Testsúlya?<br>
&ndash; Hatszor tíz a huszonnegyediken kilogramm.<br>
&ndash; Rendben, ezzel meg is volnánk... Mi a panasza?<br>
&ndash; Nos... Sokszor olyan, mintha fulladnék, és a légkörömet is egyre melegebbnek érzem.<br>
&ndash; És ez mióta tart?<br>
&ndash; Már néhány évtizede, de bíztam benne, hogy majd elmúlik.<br>
&ndash; Milyen a keringése?<br>
&ndash; Köszönöm, jó. Egy év alatt szoktam megkerülni a Napot; néha picit közelebb vagyok hozzá, néha távolabb, de azt mondják, kell a változatosság...<br>
&ndash; Végez ezen kívül más jellegű mozgást is?<br>
&ndash; Igen, naponta megfordulok a tengelyem körül.<br>
&ndash; A folyadékmennyiségre oda szokott figyelni?<br>
&ndash; Oh, hogyne! Felszínemnek mintegy 70 százalékát víz borítja. Többnyire sós ugyan, de a halak még nem panaszkodtak.<br>
&ndash; Ez a kitüremkedés már régóta itt van?<br>
&ndash; Ez itt? Jaa, ez csak a Mount Everest! Lassan alakult ki, de már megszoktam.<br>
&ndash; Tapasztalt korábban bármi mást, ami említést érdemel?<br>
&ndash; Hmm... Hát voltak jégkorszakaim... belém csapódott néhány meteorit... meg átvonult a felszínemen rengeteg vihar... a folyamatos Napszélről nem is beszélve... De ezek elhanyagolhatók.<br>
&ndash; Tart esetleg bármilyen háziállatot, amire allergiás lehet?<br>
&ndash; Van egy Holdam, de az nem sok vizet zavar... leszámítva azt a kis árapályt az óceánjaimon, de azzal nincs bajom. Úgy is mondhatnám, hogy: kölcsönös a vonzalom. Amúgy régen voltak dinoszauruszaim, de már kihaltak. Aztán megjelent rajtam valami élősdi, de nem tulajdonítottam neki túl nagy jelentőséget.<br>
&ndash; Volt mostanában hőemelkedése?<br>
&ndash; Igen, főleg geotermikus, de az már születésem óta van. De ha nagyon feldühítenek, néha még a mai napig is szoktak vulkánkitöréseim lenni. Ezt leszámítva csak villámcsapásaim vannak...<br>
&ndash; Értem. Nos, attól tartok, hogy Önnek globális felmelegedése van.<br>
&ndash; Szent univerzum! És az súlyos?<br>
&ndash; Még nem tudom. További vizsgálatok szükségesek. Szokott hordani jégsapkát?<br>
&ndash; Igen, mindig. De mintha az utóbbi időben összement volna...<br>
&ndash; Sajnos ez is a globális felmelegedés egyik mellékhatása. A fulladást a megnövekedett szén-dioxid-kibocsátás miatt érzi, ami üvegházhatást eredményez, és ez a légkör felmelegedését okozza.<br>
&ndash; És ezt mi válthatta ki?<br>
&ndash; Nos, a jelekből arra következtetek, hogy Önnek embere van. Ez lehet az az „élősdi”, amit említett. És ahogy elnézem, már rendesen elszaporodtak. Bányászatot, olajfúrást, fakitermelést okozhatnak, ebből kifolyólag jelentkezhetnek ipari forradalmak, környezetszennyezések, valamint magasabb fejlettségi szint esetén akár felszín fölött keringő műholdak is megjelenhetnek.<br>
&ndash; Áh, szóval ezek legyeskednek itt körülöttem már egy ideje!...<br>
&ndash; Így van... Továbbá érdemes lehet még tudni, hogy önpusztító fajról beszélünk, ami többnyire háborúk formájában nyilvánul meg. Ha azonban sikerül megtanulniuk a mértékletességet, akkor akár nagyon szerethetővé és az Ön szempontjából is hasznossá válhatnak. A problémát jelenleg a rengeteg gyáruk okozza, ami töméntelen mennyiségű káros anyagot bocsát ki az Ön légkörébe. Ha ezt sikerülne drasztikusan lecsökkenteni, az hosszútávon mindenképp sokat segítene az állapotán.<br>
&ndash; És így mennyi időt jósol még nekem?<br>
&ndash; A jó hír az, hogy ezzel a keringő életmóddal &ndash; még az emberektől függetlenül is &ndash; akár bő másfél milliárd évet is leélhet. De a globális felmelegedéssel akkor is kellene valamit kezdeni, hacsak az embereknek nem az a céljuk, hogy teljesen lakhatatlanná tegyék Önt.<br>
&ndash; Mit tud javasolni?<br>
&ndash; Kezdetnek felírok hétmilliárd adag környezettudatosságot, jó? Ez többféle hatóanyagot tartalmaz, többek között energiatakarékosságot, újrahasznosítást, valamint a megújuló energiaforrások használatát. Ezt naponta legyen szíves széthinteni az emberek között. Aztán reménykedünk a gyógyulásban... De azért legalább ötvenévente egyszer jöjjön be egy kontrollra, hogy lássuk, van-e javulás.<br>
&ndash; Köszönöm szépen, doktor úr! Viszontlátásra!<br>
&ndash; Minden jót! Viszontlátásra!
</p>

