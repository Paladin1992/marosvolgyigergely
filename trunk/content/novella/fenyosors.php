<h3>Fenyősors</h3>

<p>
Egy városi piactéren járunk, valamikor december közepén. A stand mögé nemrég állt be egy teherautó, arról pakolják éppen a frissen vágott fenyőfákat, mert a múlt heti adagból már alig maradt néhány. Az egyiket pont egy ilyen régebbi fa mellé támasztják, s miután a rakodók elmentek, az új fenyő megszólítja a már ott lévő, idősebb fenyőt:
</p>

<p>
&ndash; Üdvözlöm!<br>
&ndash; J-jó napot kívánok! &ndash; köszön vissza szendergéséből felriadva a másik fenyő, majd viccesen hozzáteszi &ndash; Már amennyire kivágásának napját jónak lehet mondani...<br>
&ndash; Na igen; voltak már szebb napjaim is...<br>
&ndash; Maga új itt, igaz?<br>
&ndash; Igen, most hoztak.<br>
&ndash; Csak azért kérdem, mert kicsit elbóbiskoltam, és nem nagyon figyeltem, mi történik körülöttem. Próbáltam pihenni, de akkora itt a zsibongás, hogy nem lehet. Pedig a kis tavacska mellett milyen jó csend volt!...<br>
&ndash; Magának volt szerencséje tóparton állni? &ndash; kérdezte irigykedve a fiatal fa.<br>
&ndash; Bizony ám! És hogy szerettem! Áldottam is érte a szelet, hogy ilyen finom, tápanyag-dús földre sodort engem! &ndash; majd felsóhajtva hozzátette &ndash; Hiányoznak a gyökereim...!<br>
&ndash; És egyedül volt?<br>
&ndash; Óh, szerencsére nem. Közvetlen mellettem egy tölgyfa állt &ndash; azzal osztoztunk a vízen, szinte együtt nőttünk fel &ndash;, nagyon jó barátok voltunk: mintha egy magról keltünk volna. Sokszor irigykedett, hogy a madarak inkább engem választanak fészekrakáshoz, mert sűrűbb a lombom; én meg azért irigykedtem, mert mindig az ő ágaira szálltak dalolászni; leginkább a cinkéket és a rigókat szerettem hallgatni... Mit ne mondjak: jobb volt, mint ez a városi zaj &ndash; ott legalább éjjel-nappal tudtam pihenni. És maga? Honnan jött?<br>
&ndash; Én egy gyönyörű fennsíkról, a többi társammal együtt. Ez a hely fent van magasan a hegyekben, valamivel a hóhatár alatt; onnan nagyon messzire ellátni. Velem szemben két hegycsúcs ölelkezett, és én mindig köztük néztem a lemenő napot. Nagyon szerettem a zölden virító pázsitot &ndash; főleg reggel, mert úgy imádtam felszippantani a fűszálakról lehulló harmatcseppeket!...<br>
&ndash; Igen, emlékszem az érzésre... &ndash; mondta nosztalgiázva az idősebb fenyő.<br>
&ndash; Tudja, az zavar a legjobban &ndash; folytatta a fiatalabbik &ndash;, hogy nyár végén megígértem egy kedves madárpárnak, hogy majd rám rakhatják a fészküket. Hát mit fognak rólam gondolni tavasszal, ha visszajönnek, mikor én már nem leszek ott?!<br>
&ndash; Nyugodjon meg, átérzem aggodalmát; jómagam is titkon reménykedem, hogy egyszer talán még láthatom tölgyfa barátomat...<br>
&ndash; Ha már itt tartunk, maga tudja, hogy miért vágtak ki bennünket? Hiszen én még olyan fiatal és vékonyka vagyok; deszkának se lennék jó... Tán kapanyelet akarnak belőlünk csinálni, vagy mit?<br>
&ndash; Nem, nem; karácsonyfának szántak minket.<br>
&ndash; Hogy micsodának?!<br>
&ndash; Tudja, a karácsony egy ünnep az embereknél. Úgy is szokták emlegetni, hogy „a szeretet ünnepe”. A hagyomány szerint minden család egy fenyőfát állít a házában, hogy égősort és díszeket aggasson rá, és...<br>
&ndash; Na, álljunk meg egy szóra! Most komolyan azt akarja velem elhitetni, hogy ezek azért vágják ki a mi fajtánkat, hogy aztán betuszkoljanak egy szűk szobába, míg minden méltóságunktól megfosztva úgy nézünk ki, mint egy utcai fényreklám?! Milyen megalázó!... Egyáltalán miért csak minket?! A diófák, a gesztenyefák, a nyírfák és a többi nem jó nekik? Ők megússzák?!...<br>
&ndash; A karácsonyfa &ndash; magyarázza az idősebbik &ndash; többek között az életet, az újjászületést jelképezi, mi pedig ugyebár, társainkkal ellentétben, örökzöldek vagyunk, ráadásul odavannak az illatunkért is. Érti már?<br>
&ndash; Oh... És ezt a „karácsonyt”... ezt meddig kell elviselnünk?<br>
&ndash; Én úgy tudom, hogy csak három napig tart, de utána még néhány hétig díszelgünk tovább, aztán leszednek, és... kidobnak.<br>
&ndash; Kidobnak?!<br>
&ndash; Igen. Vagy... elégetnek.<br>
&ndash; Elégetnek?! Mindjárt ledobom a tűleveleimet a döbbenettől! Méghogy „szeretet ünnepe”!<br>
</p>

<p>
Most pár percig nem szóltak egymáshoz, sorsukon tűnődve néztek maguk elé.<br>
</p>

<p>
&ndash; És... &ndash; kezdte szomorkás hangon a fiatalabbik &ndash; Önnek... mi a terve erre a... erre a... rövid időre? Mert az elmondottak alapján azt már biztosan nem élem meg, hogy tobozom legyen... Pedig annyira szerettem volna látni, ahogy elszórt magocskáim kihajtanak a földből...!<br>
&ndash; Tudom, ifjú barátom! Én sem így képzeltem a véget, noha én azért több időt tölthettem a szabad ég alatt... S hogy mi a tervem? Eleinte dacosan morogtam, hogy miért történik ez velem, de aztán mérlegeltem a helyzetet, és úgy döntöttem: inkább méltósággal kihúzom magam, végigjátszom ezt a színjátékot, és olyan karácsonyfaként fogok tündökölni, amilyet még nem láttak &ndash; bármilyen családhoz kerülök is!<br>
&ndash; Mindezek után még képes így viselni ezt a kiszolgáltatottságot, sőt, örülni neki?! Na tudja, mit fogok én tenni?! Majd...!<br>
</p>

<p>
De az indulattal telt fenyő nem tudta befejezni a mondatot, mert épp egy fiatal pár közeledett a standhoz, kettejük közt kézen fogva topogott egy kisfiú. Amikor megálltak, a fiúcska rögtön rámutatott a fiatal fenyőre, és izgatottan felkiáltott:<br>
</p>

<p>
&ndash; Anya, Apa, nézzétek! Milyen cép fenyőfa!<br>
&ndash; Tényleg gyönyörű &ndash; mondta az anyuka.<br>
&ndash; De nézd csak: az is szép ott, mellette &ndash; mutatott rá az apuka az idősebbik fenyőre.<br>
&ndash; Akkoj vigyük ej mindkettőt! &ndash; vágta rá határozottan a kisfiú.<br>
&ndash; Ugyan mit kezdenénk két karácsonyfával, kisfiam? &ndash; mondta mosolyogva az apuka, aztán eszébe jutott valami:<br>
&ndash; Drágám, nem azt mondtad, hogy anyukádék idén egy terebélyesebb fenyőt szeretnének?<br>
&ndash; De, igen... &ndash; válaszolt a feleség kissé habozva.<br>
&ndash; Na, hát akkor hívd fel őket gyorsan, hogy ne fáradjanak, mert mi már találtunk egyet!...<br>
</p>

<p>
Míg a feleség tárcsáz, az apuka elismerően megtapogatja a kisfiú kobakját:<br>
</p>

<p>
&ndash; Milyen jó, hogy elhoztunk magunkkal! Ügyes vagy! &ndash; azzal adott egy puszit a fiúcska fejére, majd odaszólt az eladónak:<br>
&ndash; Elnézést, ezt a két fenyőfát elvinnénk; köszönöm.<br>
</p>

<p>
A két fa mindeközben feszülten figyelte a történéseket. Egyszerre megszólal az idősebbik:<br>
</p>

<p>
&ndash; Mindjárt jön az eladó, hogy hálóba tegyen minket... Mit is kezdett el mondani az előbb?<br>
&ndash; Azt akartam mondani &ndash; kezdte immár higgadtan a fiatal fenyő &ndash;, hogy szenvedéseinkért cserébe majd jól összegyantázom nekik a szőnyeget, és szándékosan hamar lehullajtom a tűleveleimet, de... ezt a családot látva... mégis meggondoltam magam... Ők szép karácsonyt érdemelnek!<br>
&ndash; Vagyis? &ndash; kérdezte az idősebbik bölcs mosollyal.<br>
&ndash; Vagyis követni fogom a maga példáját, és igyekszem karácsonyfához méltóan viselkedni! &ndash; mondta diadalmasan a fiatal fenyő, majd hozzátette: &ndash; Örülök, hogy megismertem, és ha esetleg nem lenne már alkalmunk találkozni, engedje meg, hogy boldog karácsonyt kívánjak!<br>
&ndash; Részemről a megtiszteltetés, barátom, és Önnek is boldog karácsonyt!...<br>
</p>

