<p>
Nem szeretem a hétköznapokat... Még kimondani is sivár ezt a szót „hétköznapok”. Olyan üres. Olyan szürke. Olyan semmilyen. Mint az egész élet &ndash; ha az ember hagyja. Ez a szó nem ringat vagy emel fel úgy, mint az „ünnep” vagy a „béke”, melyek a mindenség örök pilléreit alkotó pillanatok. Ez a szó bedarál és összetör.
</p>

<p>
Mert egy helyen őrlődünk mi mindannyian: az idő malomkövei közt. De ez az idő nem az, aminek hiszed. Ez az idő &ndash; ez a hétköznapi &ndash;, ez más. Ez sürgető. Nyugtalanná tesz. Felkavar. Ez az idő nem hagy időt a fontos dolgokra, mert türelmetlen. Most akar mindent, azonnal. Nem tiszteli azon kevesek törekvését, akik maradandót kívánnak alkotni. Akik másért vannak itt: útmutatás vagy gyógyítás céljából.
</p>

<p>
Sokan annyira megijednek ettől az „ál-időtől”, hogy inkább behódolva neki, ugyanazt fújják vele, és kizökkentik a magamfajtát a mindenség dallamos ritmusából &ndash; pedig mindenkinek ezt kellene követnie.
</p>

<p>
Nekik hétfő, kedd, szerda stb. jut. Nekem: az örökkévalóság. És ezért sürgetnek, hogy „ezt kell” meg „azt kell” &ndash; de nem. Ez csupán az ő szükségük, mégis mindenkire rá akarják erőltetni, mert nem bírják elviselni, ha valaki kilóg a sorból. Más az én időm, mint az övéké. De ők ezt nem értik. Pedig bármikor kiléphetnének a könnyű felismerés útjára. Bármikor. De nem akarják. Nem mernek akarni. Nem is tudják, hogy akarhatják. Mert úgy érzik, biztonságban vannak a mókuskerékben. Számomra a biztonságot maga a szabadság jelenti &ndash; ezt viszont csak az öröklét vizén érzem. Hajójuk nekik is meglenne hozzá, de ők inkább partra vetődve keresgélnek múló földi kincsek után, a múlhatatlan égiek helyett.
</p>

<p>
Egyszerűen nem értenek. Meg sem próbálják. Inkább ostobának néznek. Félnek. És ez a félelem az, mely arra készteti őket, hogy kiirtsák bennem a szellemiség által szült virágokat, összetörjék égig érő létrámat, melyen át én bármikor kiléphetek az igazi világba, amiről ők talán egész életükben még tudomást sem fognak szerezni. De ők nem értik ezt. Sose fogják.
</p>

<p>
Nem baj. Nyugodt vagyok. Én a béke egyetlen, örök napját élem, mert tudom: nem az egyén a különc, hanem a sokaság.
</p>

