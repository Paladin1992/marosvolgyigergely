<p>
Egy átlagos családi házban vagyunk, nem sokkal a fadíszítés befejezte után. A szoba csendes, a sarokban áll a karácsonyfa. Egyszer csak megszólal az egyik aranyszínű dísze:
</p>

<p>
&ndash; Psszt! Hahó! Hé, te piros! Hé, te gömb!... Piros gömb! Gömbike!... Gömböcske!...<br>
Mire a gömb mogorván felhördül:<br>
&ndash; Mi van?!!<br>
&ndash; Nem cserélünk ágat?<br>
&ndash; Ágat?! Minek? Nem jó ott, ahol vagy?!<br>
&ndash; Nem, mert itt folyton a szemembe süt a Nap.<br>
&ndash; Néhány óra, és lemegy. Bírd ki!<br>
&ndash; De annyira zavar! Naa, cseréljünk helyet!<br>
&ndash; ...Hogy inkább az én szemembe süssön, mi? Nahát, milyen kedves tőled! És ez hol érné meg nekem? Még ha bele is egyeznék, úgy őszintén, szerinted hogyan tudnánk ágat cserélni? Hiszen még kezünk sincs, mint az embereknek!<br>
&ndash; Bátornak nincs lehetetlen! &ndash; mosolygott kacsintva a másik dísz. &ndash; Naaa? Légyszi!<br>
&ndash; Hagyjál már lógni nyugodtan!<br>
&ndash; Most mit vagy úgy oda? Olyan nagy kérés ez?! Karácsony van! Nekem csak ennyi lenne a vágyam...<br>
&ndash; Nekem meg annyi lenne a vágyam, hogy hagyjanak végre békén!... Ott lóg az a hóember, vele miért nem akarsz cserélni? Az ő helye is biztos nagyon jó lehet...
</p>

<p>
Erre a mondatra a hóember alakú dísz megvetően feléjük nézett, majd rideg tekintettel, gombszemeit forgatva, tiltakozva fordult el a másik irányba. Most egy magasabban függeszkedő angyalka szólalt meg fölöttük:
</p>

<p>
&ndash; Jaj, ugyan már, srácok! Ne veszekedjetek ilyen apróságon! Jövőre majd úgyis mindenki máshol lesz! &ndash; Ránézett a vitatkozó díszre: &ndash; Fogadd el azt az ágat, ahol éppen vagy. Biztosan nem volt véletlen, hogy pont oda tettek.<br>
&ndash; Na, nézd már! Megszólalt a mi jótét lélek hófehérkénk!... Pedig igazán az én pártomat foghatnád, ha már úgyis mindig azt mondogatod, hogy a változás olyan fontos! Tessék: tenni akarok a változásért azzal, hogy a másik ágon szeretnék lógni!...<br>
</p>

<p>
Az angyalka szomorúan fejét csóválta, de azonnal védelmére kelt az égősor egyik égője:<br>
&ndash; Hé! Micsoda modor ez?! Fogd már vissza magad!<br>
&ndash; Nekem villogsz, lámpika?<br>
&ndash; Lámpikaaa?!
</p>

<p>
Erre a hangzavarra már odafordult egy szaloncukor is:
</p>

<p>
&ndash; Skacok, min veszekedtek ennyire? Lógjatok már nyugiban! Az angyalkának igaza van: jövőre úgyis minden máshogy lesz.<br>
&ndash; Mondja ezt az, akit még tán az idén megesznek! &ndash; vágott vissza az aranydísz.<br>
&ndash; Micsoda?! &ndash; lepődött meg a szaloncukor. Erre a dísz felbátorodva folytatta:<br>
&ndash; Hát nem is tudsz róla? Neked annyi! Megesznek, haver!... &ndash; és rosszmájúan felnevetett.
</p>

<p>
Egyre több dísz és szaloncukor csatlakozott a vitához, a zaj pedig nőttön-nőtt. Mindenki mondta a magáét; ki csillapította, ki csak jobban borzolta a kedélyeket.<br>
</p>

<p>
A fenyőfa eleinte türelmes volt. Várta, hogy talán a díszek intelligensen megoldják e konfliktust, de sajnos nem így nézett ki. Ő csak némi nyugalomra vágyott volna, semmi többre. A zaj nem akart csillapodni, egyre harsányabban veszekedtek a díszek.<br>
</p>

<p>
Végül eljött a pillanat, amikor a fenyőfa nem bírta tovább: megelégelte a veszekedést, és egy laza mozdulattal rántott egyet azon az ágán, melyre a vitát kirobbantó aranydísz volt felakasztva, mire az leesett a földre és összetört. A többi dísz riadva meghökkent, mire a fa így szólt:<br>
&ndash; Van még kérdés?
</p>

<p>
A díszek zavart mosollyal rázták a fejüket, mutatván, hogy minden a legnagyobb rendben (bár valamennyire fel is lélegeztek, hogy végre csönd van). Aztán hirtelen a törött dísz hangja szólalt meg a földről:<br>
&ndash; Háhá! Még mindig tudok beszélni!...
</p>

<p>
Erre mindenki keserűen felsóhajtott: „Hosszú lesz ez a karácsony!...”
</p>

