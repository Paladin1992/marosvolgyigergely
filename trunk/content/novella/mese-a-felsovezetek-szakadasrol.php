<h3>Mese a felsővezeték-szakadásról</h3>

<p>
Hol volt, hol nem volt, volt egyszer egy felsővezeték-szakadás. Egy hétfői reggelen a kis felsővezeték-szakadás úgy döntött, elmegy világot látni. Búcsút intett hát szüleinek, és elindult Vezetékföldéről, hogy kalandot keressen.
</p>

<p>
Ment, mendegélt, míg el nem ért egy vasúti töltéshez. A sínek mentén villanyoszlopokat látott sorakozni, akik gyanakvó tekintettel néztek rá, mert érezték, hogy amaz valami rosszban sántikál. Féltették szeretett magasfeszültségű vezetéküket. Ő viszont sok-sok éven át edzett és tanult, hogy a megfelelő helyen és időben majd szüleihez méltó szakadás válhasson belőle. Úgy gondolta, túl messzire jött már el ahhoz, hogy most feladja, és megriadjon néhány maradi tartóoszloptól! Az volt a terve, hogy amint a legközelebbi vonat áramszedője „lőtávolba” ér, ő legjobb tudása szerint &ndash; ahogy az örege tanította &ndash; le fogja szakítani a vezetéket, hogy beteljesítse sorsát.
</p>

<p>
Így is lett. A vonat nagy fékezés után hirtelen megállt, a vezető tájékoztatta az utasokat a történtekről. A kis felsővezeték-szakadás pedig ott viháncolt fönn magában. Gondolta: ha most látnák, nagyon büszkék lennének rá a szülei. Úgy választott helyet, ahogy az apja mutatta neki mindig, és olyan szenvedélyesen nyúlt el több méter hosszan, hogy azt még drága anyukája is megirigyelhetné!
</p>

<p>
Aztán egyszer csak elkezdett esni, és egyre hevesebben zúdultak alá az esőcseppek. A fiatal felsővezeték-szakadás nem tudta, mi ez, ezért hangosan megkérdezte:<br>
&ndash; Te ki vagy, és mit csinálsz?<br>
&ndash; Én az eső vagyok, és ugyanazt csinálom, amit te: szakadok.<br>
A felsővezeték-szakadás megörült ennek a kijelentésnek, azonnal barátságot kötöttek, és együtt szakadtak tovább &ndash; a vasúti karbantartók mérgére, mert az eső megnehezítette számukra a javítási munkálatokat.
</p>

<p>
Az utasok legtöbbje &ndash; köztük jómagam is &ndash; már leszállt a vonatról, és (szitkozódások közepette) próbált valami alternatív módot keresni az út folytatására. Több órára megbénult a vasúti forgalom, míg végül a villanyoszlopok fellélegezhettek, hogy szívüknek oly kedves felsővezetéküket újra épnek és egészségesnek láthatják &ndash; sőt, délutánra még a nap is kisütött.
</p>

<p>
Ha egy órás késéssel a munkahelyemre be nem értem volna, az én mesém is tovább tartott volna.
</p>

