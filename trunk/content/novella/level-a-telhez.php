<p>
Kedves Tél Úr!
</p>

<p>
Én írok levelet magának. Kell több? Nem mond ez eleget?!... Elkeseredésemben fordulok Önhöz, ugyanis felettébb aggasztó, ami az utóbbi időben történik kicsiny országunk légkörében. Természetesen tisztában vagyok azzal, hogy Uraságod hivatali ideje minden év decembertől a rá következő év februárjáig tart, és hogy a naptár sem pontos irányadó, de mégis arra kérném, hogy viseltessen némi tisztelettel azok iránt, akik már &ndash; teljesen jogosan &ndash; a kikeletet várják ilyenkor! (Nos, itt szeretném egészen őszintén bevallani &ndash; és ne vegye sértésnek &ndash;, hogy én is közéjük tartozom).
</p>

<p>
Ugyanis az elmúlt néhány napban már sütött a Nap, végre lehetett a kék ég tisztaságában gyönyörködni, és a madarak is rákezdtek már tavaszi nótájukra (magunk közt szólva némelyiknek elég gyér a repertoárja, de kétség sem férhet hozzá, hogy azt a néhányat virtuóz módjára tudják egy faágon hintázva előadni!). Na most gondolja el, kedves Tél Úr, hogy mikor valaki a fentiek tudatában kezdi beleélni magát ezekbe a gyönyörűségekbe, amit a tavasz kínál: új remények, új szerelmek, új lehetőségek, lengébb ruhatár stb., és már csak az alkalomra vár, hogy megragadja azt, erre... erre egy reggel arra kel az ember, hogy odakint hatalmas pelyhekben szakad a hó! Rendben, el kell ismerni, tényleg szép látvány volt, de képzelje csak el azt a sok csalódott arcot!...
</p>

<p>
Engedje meg, hogy akkor itt feltegyem az ominózus kérdést: miért nem lehetett ezzel megörvendeztetni a népet mondjuk karácsony idején, amikor úgy vártuk?!... Sajnálattal közlöm, hogy most már nem tartunk igényt szolgáltatásaira! Tisztában vagyok vele, hogy a hópihék előállítása költséges és bonyolult folyamat, de azért a megszokott rendet jó lenne betartani! Mert ha mondjuk az történt, hogy maradt raktáron némi hó korábbról, és leltározás miatt el kellett szórni valahol... &ndash; ám legyen, megértem, elfogadom, és a dolog tárgytalan. De ha csak úgy szeszélyes kedvének engedett teret azon a reggelen, vagy csak úgy ránk akart poénból ijeszteni, akkor kérem szépen, hogy legközelebb ne tegyen ilyet!
</p>

<p>
Szóval mindent összevetve az lenne a legfontosabb, hogy &ndash; még ha jégből is van &ndash;: legyen szíve! Csomagolja össze hűvös szeleit, a hópelyheket, az esőcseppeket, a didergést, a szomorúságot, és menjen el telelni máshova, mondjuk úgy november végéig. Ön jó sokáig pihenhet, mi meg nem szidjuk magát, és így mindenki jól jár! Kérem, fontolja meg levelemben megfogalmazott gondolataimat, és ehhez mérten cselekedjen.
</p>

